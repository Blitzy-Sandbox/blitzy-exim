/// Generate a complete Exim message ID in the current format.
///
/// Produces a 23-character message ID in the format `TTTTTT-PPPPPPPPPPP-SSSS`:
///
/// - **Time** (`T`, 6 chars): Base-62 encoded seconds since Unix epoch via
///   [`string_base62_32`].
/// - **PID** (`P`, 11 chars): Base-62 encoded process ID via
///   [`string_base62_64`].
/// - **Sub-second** (`S`, 4 chars): Base-62 encoded sub-second component,
///   derived from either:
///   - **With `host_number`**: `host_number × (1000000 / id_resolution) +
///     tv_usec / id_resolution` — encodes both host identity and sub-second
///     precision into a single field.
///   - **Without `host_number`**: `tv_usec / id_resolution` — encodes only
///     the fractional time at the configured resolution.
///
///   In both cases, the full 6-character base-62 output is computed, and only
///   the last [`MESSAGE_ID_SUBTIME_LEN`] (4) characters are used (offset
///   `6 − 4 = 2`, i.e., bytes `[2..6]`).
///
/// This is the Rust equivalent of the message ID generation in
/// `src/src/receive.c` lines 2799–2825.
///
/// # Arguments
///
/// * `tv_sec` — Seconds since Unix epoch (from `gettimeofday`).
/// * `pid` — Process ID of the receiving process (64-bit to support modern
///   kernels with large PID namespaces).
/// * `tv_usec` — Microseconds component of the timestamp (0–999999).
/// * `host_number` — Optional host number for multi-host setups. When
///   `Some(n)`, the sub-second component encodes both the host number and
///   the fractional time. The value is validated at config read time to
///   ensure the calculation does not overflow.
/// * `id_resolution` — Resolution divisor for the sub-second component.
///   Typically 1 (microsecond resolution) or higher for coarser granularity.
///   Must be non-zero.
///
/// # Panics
///
/// Panics if `id_resolution` is zero (division by zero).
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::generate_message_id;
///
/// let id = generate_message_id(1_700_000_000, 12345, 500_000, None, 1);
/// assert_eq!(id.len(), 23);
/// assert_eq!(id.as_bytes()[6], b'-');
/// assert_eq!(id.as_bytes()[18], b'-');
/// ```
pub fn generate_message_id(
    tv_sec: u32,
    pid: u64,
    tv_usec: u32,
    host_number: Option<u32>,
    id_resolution: u32,
) -> String {
    // Pre-allocate the exact capacity for the 23-character message ID
    let mut result = String::with_capacity(MESSAGE_ID_LENGTH);

    // ── Time component (6 chars) ────────────────────────────────────────────
    // C: Ustrncpy(message_id, string_base62_32((long int)(message_id_tv.tv_sec)),
    //            MESSAGE_ID_TIME_LEN);
    let time_chars = string_base62_32(tv_sec);
    for &b in &time_chars {
        result.push(b as char);
    }

    // ── First separator ─────────────────────────────────────────────────────
    // C: message_id[MESSAGE_ID_TIME_LEN] = '-';
    result.push('-');

    // ── PID component (11 chars) ────────────────────────────────────────────
    // C: Ustrncpy(message_id + MESSAGE_ID_TIME_LEN + 1,
    //            string_base62_64((long int)getpid()), MESSAGE_ID_PID_LEN);
    let pid_chars = string_base62_64(pid);
    for &b in &pid_chars {
        result.push(b as char);
    }

    // ── Second separator ────────────────────────────────────────────────────
    // C: sprintf format begins with "-"
    result.push('-');

    // ── Sub-second component (4 chars) ──────────────────────────────────────
    // Compute the sub-second value. Use u64 intermediate to avoid potential
    // overflow when host_number is large, matching the C behavior where the
    // computation occurs in `long int` (64-bit on 64-bit systems) before
    // being passed to string_base62_32.
    let subtime_value: u32 = match host_number {
        Some(hn) => {
            // C: host_number * (1000000/id_resolution)
            //    + message_id_tv.tv_usec/id_resolution
            let val = u64::from(hn) * (1_000_000u64 / u64::from(id_resolution))
                + u64::from(tv_usec) / u64::from(id_resolution);
            val as u32
        }
        None => {
            // C: message_id_tv.tv_usec/id_resolution
            tv_usec / id_resolution
        }
    };

    // Encode to 6 base-62 characters, then take the last MESSAGE_ID_SUBTIME_LEN (4)
    // C: string_base62_32(...) + (6 - MESSAGE_ID_SUBTIME_LEN)
    let subtime_full = string_base62_32(subtime_value);
    let offset = MESSAGE_ID_TIME_LEN - MESSAGE_ID_SUBTIME_LEN; // 6 - 4 = 2
    for &b in &subtime_full[offset..] {
        result.push(b as char);
    }

    debug_assert_eq!(
        result.len(),
        MESSAGE_ID_LENGTH,
        "generated message ID must be exactly {} characters",
        MESSAGE_ID_LENGTH
    );
    result
}

