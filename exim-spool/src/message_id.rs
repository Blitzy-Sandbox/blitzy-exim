// =============================================================================
// Base-62 Character Set
// =============================================================================
//
// Exim encodes message ID components using a base-62 character set. The
// standard set (BASE_62 == 62) uses digits, uppercase, and lowercase letters.
// An alternative base-36 set (digits + uppercase only) exists for
// case-insensitive filesystems (defined in exim.h with `#if BASE_62 == 62`),
// but is very rarely used in practice.
//
// Source: `src/src/globals.c` lines 544–549:
//   #if BASE_62 == 62
//   uschar *base62_chars =
//       "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
//   #else
//   uschar *base62_chars = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
//   #endif
//
// To support base-36 on case-insensitive filesystems, add a `base36` feature
// flag to the exim-spool Cargo.toml and use `#[cfg(feature = "base36")]`
// guards around `BASE` and `BASE_CHARS` definitions.

/// The number of distinct characters in the encoding alphabet.
///
/// Default value is 62 (digits + uppercase + lowercase). For case-insensitive
/// filesystems this would be 36 (digits + uppercase only), matching the C
/// `BASE_62` compile-time constant.
const BASE: usize = 62;

/// The ordered character set for base-62 encoding.
///
/// Index 0 = `'0'`, index 9 = `'9'`, index 10 = `'A'`, index 35 = `'Z'`,
/// index 36 = `'a'`, index 61 = `'z'`.
///
/// Source: `src/src/globals.c` lines 544–546.
const BASE_CHARS: &[u8; 62] = b"0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";

// =============================================================================
// Base-62 Encoding Functions
// =============================================================================

/// Encode a 32-bit value to exactly 6 base-62 characters.
///
/// This is the Rust equivalent of C `string_base62_32()` from
/// `src/src/string.c` lines 224–236. The output is always exactly
/// [`MESSAGE_ID_TIME_LEN`](crate::format::MESSAGE_ID_TIME_LEN) (6)
/// characters. When the value is smaller than 62^6, leading positions are
/// filled with `'0'` (the first character of the base-62 charset).
///
/// # Algorithm
///
/// Fills a fixed 6-byte array from right to left using modular arithmetic:
/// `result[i] = BASE_CHARS[value % 62]` then `value /= 62`. When `value`
/// reaches zero, subsequent positions receive `BASE_CHARS[0]` (`'0'`) because
/// `0 % 62 == 0`.
///
/// This matches the C implementation which uses a static `yield[7]` buffer
/// (6 chars + null terminator) and fills via `while (p > yield)` — always
/// exactly 6 iterations regardless of the input value.
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::string_base62_32;
///
/// // Zero encodes to six '0' characters
/// assert_eq!(&string_base62_32(0), b"000000");
///
/// // Value 1 encodes with leading zeros
/// assert_eq!(&string_base62_32(1), b"000001");
///
/// // Value 62 wraps to next position
/// assert_eq!(&string_base62_32(62), b"000010");
/// ```
pub fn string_base62_32(mut value: u32) -> [u8; 6] {
    // C reference (src/src/string.c lines 227–234):
    //   static uschar yield[7];
    //   uschar * p = yield + sizeof(yield) - 1;
    //   *p = 0;
    //   while (p > yield)
    //     { *--p = base62_chars[value % BASE_62]; value /= BASE_62; }
    let base = BASE as u32;
    let mut result = [0u8; 6];
    for i in (0..6).rev() {
        result[i] = BASE_CHARS[(value % base) as usize];
        value /= base;
    }
    result
}

/// Encode a 64-bit value to exactly 11 base-62 characters, zero-padded.
///
/// This is the Rust equivalent of C `string_base62_64()` from
/// `src/src/string.c` lines 238–253. The output is always exactly
/// [`MESSAGE_ID_PID_LEN`](crate::format::MESSAGE_ID_PID_LEN) (11)
/// characters. Positions where the value has been fully consumed are filled
/// with `'0'` (the first character of the base-62 charset).
///
/// # Algorithm
///
/// Fills a fixed 11-byte array from right to left. While `value > 0`:
/// `result[i] = BASE_CHARS[value % 62]` then `value /= 62`. Once `value`
/// reaches zero, remaining positions are set to `'0'`.
///
/// This matches the C implementation which explicitly zero-pads:
/// ```text
/// while (p > yield)
///   if (value) { *--p = base62_chars[value % BASE_62]; value /= BASE_62; }
///   else *--p = '0';
/// ```
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::string_base62_64;
///
/// // Zero encodes to eleven '0' characters
/// assert_eq!(&string_base62_64(0), b"00000000000");
///
/// // Small value with leading zero-padding
/// assert_eq!(&string_base62_64(1), b"00000000001");
/// ```
pub fn string_base62_64(mut value: u64) -> [u8; 11] {
    // C reference (src/src/string.c lines 241–252):
    //   static uschar yield[12];
    //   uschar * p = yield + sizeof(yield) - 1;
    //   *p = '\0';
    //   while (p > yield)
    //     if (value)
    //       { *--p = base62_chars[value % BASE_62]; value /= BASE_62; }
    //     else
    //       *--p = '0';
    let base = BASE as u64;
    let mut result = [b'0'; 11]; // Pre-fill with '0' for zero-padding
    for i in (0..11).rev() {
        if value > 0 {
            result[i] = BASE_CHARS[(value % base) as usize];
            value /= base;
        }
        // When value == 0, the pre-filled '0' is already correct,
        // matching the C `else *--p = '0'` branch.
    }
    result
}

