/// Format a message ID for external use (e.g., in `Received:` headers).
///
/// Prepends `'E'` to the internal message ID to ensure it starts with a
/// letter, as required by some implementations (notably Smail). The internal
/// spool file names do **not** include this leading letter.
///
/// From `src/src/receive.c` lines 2786–2790:
///
/// > "There doesn't seem to be anything in the RFC which requires a message
/// > id to start with a letter, but Smail was changed to ensure this. The
/// > external form of the message id (as supplied by string expansion)
/// > therefore starts with an additional leading 'E'. The spool file names
/// > do not include this leading letter and it is not used internally."
///
/// # Arguments
///
/// * `id` — The internal message ID (e.g., `"TTTTTT-PPPPPPPPPPP-SSSS"`).
///
/// # Returns
///
/// A [`String`] with `'E'` prepended (e.g., `"ETTTTTT-PPPPPPPPPPP-SSSS"`).
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::external_message_id;
///
/// let ext = external_message_id("1abCDE-000000001s3-ABCD");
/// assert_eq!(ext, "E1abCDE-000000001s3-ABCD");
/// ```
pub fn external_message_id(id: &str) -> String {
    let mut result = String::with_capacity(1 + id.len());
    result.push('E');
    result.push_str(id);
    result
}

