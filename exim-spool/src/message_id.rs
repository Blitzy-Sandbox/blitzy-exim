/// Detect if a message ID is in the current (new) format.
///
/// The new format has a dash (`'-'`) at position
/// `MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN` (= 6 + 1 + 11 = **18**),
/// which is the separator before the 4-character sub-second component.
///
/// This is the Rust equivalent of the C inline function `is_new_message_id()`
/// from `src/src/functions.h` lines 1245–1249:
///
/// ```text
/// static inline BOOL is_new_message_id(const uschar * id) {
///     return id[MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN] == '-';
/// }
/// ```
///
/// Unlike the C version, this function performs bounds checking via
/// [`slice::get`] and safely returns `false` for strings shorter than 19
/// characters.
///
/// # Arguments
///
/// * `id` — A string that may be a message ID. May be shorter than the
///   expected length, in which case the function safely returns `false`.
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::is_new_message_id;
///
/// // New-format ID: dash at position 18
/// assert!(is_new_message_id("AAAAAA-00000000000-BBBB"));
///
/// // Old-format ID: no dash at position 18
/// assert!(!is_new_message_id("AAAAAA-BBBBBB-CC"));
///
/// // Short string: safely returns false
/// assert!(!is_new_message_id("short"));
/// ```
pub fn is_new_message_id(id: &str) -> bool {
    // C: return id[MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN] == '-';
    // Position: 6 + 1 + 11 = 18
    id.as_bytes()
        .get(MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN)
        == Some(&b'-')
}

/// Detect if a message ID is in the old (legacy) format.
///
/// The old format has a dash (`'-'`) at position
/// `MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN_OLD` (= 6 + 1 + 6 = **13**),
/// which is the separator before the 2-character sub-second component.
///
/// This is the Rust equivalent of the C inline function `is_old_message_id()`
/// from `src/src/functions.h` lines 1251–1255:
///
/// ```text
/// static inline BOOL is_old_message_id(const uschar * id) {
///     return id[MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN_OLD] == '-';
/// }
/// ```
///
/// Unlike the C version, this function performs bounds checking via
/// [`slice::get`] and safely returns `false` for strings shorter than 14
/// characters.
///
/// # Arguments
///
/// * `id` — A string that may be a message ID. May be shorter than the
///   expected length, in which case the function safely returns `false`.
///
/// # Examples
///
/// ```
/// use exim_spool::message_id::is_old_message_id;
///
/// // Old-format ID: dash at position 13
/// assert!(is_old_message_id("AAAAAA-BBBBBB-CC"));
///
/// // New-format ID: no dash at position 13
/// assert!(!is_old_message_id("AAAAAA-00000000000-BBBB"));
///
/// // Short string: safely returns false
/// assert!(!is_old_message_id("short"));
/// ```
pub fn is_old_message_id(id: &str) -> bool {
    // C: return id[MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN_OLD] == '-';
    // Position: 6 + 1 + 6 = 13
    id.as_bytes()
        .get(MESSAGE_ID_TIME_LEN + 1 + MESSAGE_ID_PID_LEN_OLD)
        == Some(&b'-')
}

