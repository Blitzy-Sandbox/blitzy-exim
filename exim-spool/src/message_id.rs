        // Time component: string_base62_32(1700000000) = "1r31eq"
        assert_eq!(&id[0..6], "1r31eq");
        // PID component: string_base62_64(12345) = "000000003D7"
        assert_eq!(&id[7..18], "000000003D7");
        // Sub-second without host_number: string_base62_32(500000/1) = string_base62_32(500000)
        // 500000 / 62 = 8064, remainder 32 → 'W'
        // 8064 / 62   = 130, remainder 4 → '4'
        // 130 / 62    = 2, remainder 6 → '6'
        // 2 / 62      = 0, remainder 2 → '2'
        // Full: "0026" + "4W" → "0026" part starting at offset 2
        // Actually: string_base62_32(500000) then take last 4 chars
        // 500000 in base 62:
        //   500000 / 62 = 8064 r 32 → 'W'
