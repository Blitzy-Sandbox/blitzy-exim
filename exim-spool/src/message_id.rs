//! Exim message ID generation using base-62 encoding.
//!
//! The message ID uniquely identifies each message in the spool and is used as
//! the filename stem for `-H` (header/metadata) and `-D` (data) files.
//!
//! ## Format
//!
//! **Current format:** `TTTTTT-PPPPPPPPPPP-SSSS` (23 characters)
//!
//! - `T` = base-62 encoded seconds since Unix epoch (6 characters)
//! - `P` = base-62 encoded process ID (11 characters, zero-padded)
//! - `S` = base-62 encoded sub-second component (4 characters)
//!
//! **Legacy format:** `TTTTTT-PPPPPP-SS` (16 characters)
//!
//! - `T` = base-62 encoded seconds since Unix epoch (6 characters)
//! - `P` = base-62 encoded process ID (6 characters, zero-padded)
//! - `S` = base-62 encoded sub-second component (2 characters)
//!
//! The external form (used in `Received:` headers and `${message_exim_id}`)
//! prepends `'E'` to ensure the ID starts with a letter.
//!
//! **CRITICAL RULE (AAP §0.7.1):** Message IDs generated by Rust MUST be
//! format-identical to those produced by C Exim. The existing test harness
//! validates message ID format.
//!
//! **CRITICAL RULE (AAP §0.7.5):** CLI flags, exit codes, and log output
//! format MUST be preserved — message IDs appear in main log, reject log,
//! and panic log entries.
//!
//! ## Source origins
//!
//! - `src/src/receive.c` lines 2780–2825 — Message ID generation logic
//! - `src/src/string.c` lines 224–253 — `string_base62_32()` and
//!   `string_base62_64()`
//! - `src/src/local_scan.h` lines 114–129 — Message ID length constants
//! - `src/src/globals.c` lines 544–549 — `base62_chars` character set
//! - `src/src/functions.h` lines 1246–1262 — `is_new_message_id()`,
//!   `is_old_message_id()`, `spool_data_start_offset()`

