/// Trait for router driver implementations.
///
/// Replaces the C `router_info` struct and its function pointers (structs.h
/// lines 372-387). Each router type (accept, dnslookup, ipliteral, iplookup,
/// manualroute, queryprogram, redirect) implements this trait.
///
/// # C Equivalent
///
/// ```c
/// typedef struct router_info {
///   driver_info drinfo;
///   int (*code)(router_instance *, address_item *, struct passwd *, int,
///               address_item **, address_item **, address_item **, address_item **);
///   void (*tidyup)(router_instance *);
///   int ri_flags;
/// } router_info;
/// ```
///
/// The C `code()` function takes 8 parameters:
///   1. `router_instance *` → `&RouterInstanceConfig` (this router's config)
///   2. `address_item *`    → `&str` (the address being routed)
///   3. `struct passwd *`   → `Option<&str>` (local user, if `check_local_user`)
///   4. `int`               → absorbed into the config/context
///   5. through 8. `address_item **` → encoded in the `RouterResult` variants
///
/// The condensed Rust API captures the same semantics with a cleaner signature.
///
/// # Thread Safety
///
/// Router drivers must be `Send + Sync` because the driver registry is static
/// and accessed from multiple forked processes (though not truly concurrent
/// within a single process due to Exim's fork-per-connection model).
pub trait RouterDriver: Send + Sync + fmt::Debug {
    /// Main routing entry point — evaluates whether this router handles the
    /// given address and determines the delivery destination.
    ///
    /// # Arguments
    ///
    /// * `config` — This router instance's configuration from the Exim config file.
    /// * `address` — The email address being routed (may include prefix/suffix).
    /// * `local_user` — Local system user name if `check_local_user` matched,
    ///   `None` otherwise. In C, this was the `struct passwd *` parameter.
    ///
    /// # Returns
    ///
    /// * `Ok(RouterResult::Accept { .. })` — Route accepted with transport assignment.
    /// * `Ok(RouterResult::Pass)` — Explicitly pass to next router.
    /// * `Ok(RouterResult::Decline)` — Router not applicable for this address.
    /// * `Ok(RouterResult::Fail { .. })` — Permanent routing failure.
    /// * `Ok(RouterResult::Defer { .. })` — Temporary failure, retry later.
    /// * `Ok(RouterResult::Error { .. })` — Internal error.
    /// * `Ok(RouterResult::Rerouted { .. })` — Address redirected to new addresses.
    /// * `Err(DriverError)` — Driver-level error (not found, init failed, etc.).
    fn route(
        &self,
        config: &RouterInstanceConfig,
        address: &str,
        local_user: Option<&str>,
    ) -> Result<RouterResult, DriverError>;

    /// Tidyup function called during process cleanup or between messages.
    ///
    /// Replaces C: `void (*tidyup)(router_instance *)`.
    ///
    /// Most routers do not need cleanup — the default implementation is a no-op.
    /// Routers that cache state or hold resources should override this method.
    fn tidyup(&self, _config: &RouterInstanceConfig) {
        // Default no-op — most routers don't need cleanup.
    }

    /// Returns the descriptor flags for this router driver type.
    ///
    /// Replaces C: `ri_flags` field in `router_info`.
    ///
    /// Default returns `RouterFlags::NONE`. Drivers with special characteristics
    /// should override this method.
    fn flags(&self) -> RouterFlags {
        RouterFlags::NONE
    }

    /// Returns the canonical driver name for identification.
    ///
    /// This must match the `name` field of the corresponding `RouterDriverFactory`
    /// and the `driver = <name>` directive in configuration files.
    ///
    /// Examples: `"accept"`, `"dnslookup"`, `"ipliteral"`, `"iplookup"`,
    /// `"manualroute"`, `"queryprogram"`, `"redirect"`.
    fn driver_name(&self) -> &str;
}

