/// Trait for router driver implementations.
///
/// Replaces C `router_info` struct function pointers (structs.h lines 372-387):
/// ```c
/// typedef struct router_info {
///   driver_info drinfo;
///   int (*code)(router_instance *, address_item *, struct passwd *, int,
///               address_item **, address_item **, address_item **, address_item **);
///   void (*tidyup)(router_instance *);
///   int ri_flags;
/// } router_info;
/// ```
///
/// Each router (accept, dnslookup, manualroute, redirect, etc.) implements this trait.
pub trait RouterDriver: Send + Sync + std::fmt::Debug {
    /// Main routing entry point.
    ///
    /// Replaces C: `int (*code)(router_instance *, address_item *, struct passwd *, int, ...)`
    ///
    /// Evaluates whether this router handles the given address and determines
    /// the delivery destination (transport + host list for remote, or local transport).
    fn route(
        &self,
        config: &RouterInstanceConfig,
        address: &str,
        local_user: Option<&str>,
    ) -> Result<RouterResult, DriverError>;

    /// Tidyup function called during cleanup.
    ///
    /// Replaces C: `void (*tidyup)(router_instance *)`.
    fn tidyup(&self, _config: &RouterInstanceConfig) {
        // Default no-op
    }

    /// Router descriptor flags.
    ///
    /// Replaces C: `ri_flags` in `router_info`.
    fn flags(&self) -> RouterFlags {
        RouterFlags::NONE
    }

    /// Driver name for identification (e.g., "accept", "dnslookup", "redirect").
    fn driver_name(&self) -> &str;
}

