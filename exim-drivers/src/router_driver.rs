/// Factory for creating `RouterDriver` instances at runtime.
///
/// Registered at compile time via `inventory::submit!` in each router
/// implementation crate. The `DriverRegistry` (in `registry.rs`) collects all
/// submitted factories and provides runtime lookup by name for config-driven
/// router instantiation.
///
/// # Registration Example
///
/// ```ignore
/// use exim_drivers::router_driver::{RouterDriverFactory, RouterDriver};
///
/// inventory::submit! {
///     RouterDriverFactory {
///         name: "accept",
///         create: || Box::new(AcceptRouter::new()),
///         avail_string: None,
///     }
/// }
/// ```
///
/// # Lifecycle
///
/// 1. At compile time, `inventory::submit!` registers factories in all linked crates.
/// 2. At startup, `DriverRegistry::find_router("name")` looks up the factory.
/// 3. The `create` function is called to produce a `Box<dyn RouterDriver>`.
/// 4. The driver is paired with a `RouterInstanceConfig` from the config parser.
/// 5. `route()` is called for each address being routed through this instance.
pub struct RouterDriverFactory {
    /// Canonical name of the router driver type.
    ///
    /// Must match the `driver = <name>` directive in Exim configuration files.
    /// Examples: `"accept"`, `"dnslookup"`, `"ipliteral"`, `"iplookup"`,
    /// `"manualroute"`, `"queryprogram"`, `"redirect"`.
    pub name: &'static str,

    /// Factory function that creates a new, default-configured router driver instance.
    ///
    /// The returned driver is stateless — all per-instance configuration is stored
    /// in `RouterInstanceConfig` and passed to `route()` on each invocation.
    pub create: fn() -> Box<dyn RouterDriver>,

    /// Optional display string shown in `-bV` (version) output.
    ///
    /// If `None`, the `name` field is used for display. If `Some`, this string
    /// is displayed instead (matching C's `avail_string` in `driver_info`).
    pub avail_string: Option<&'static str>,
}

