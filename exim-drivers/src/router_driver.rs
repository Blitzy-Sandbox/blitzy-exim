/// Result of router evaluation for a single address.
///
/// Maps to C router return codes:
///   - OK (0) = route accepted, address assigned a transport
///   - PASS (5) = pass to next router
///   - DECLINE (4) = router does not handle this address
///   - FAIL (2) = routing failed permanently
///   - DEFER (1) = temporary failure, retry later
///   - ERROR (3) = internal error
///
/// Per AAP §0.4.1: "RouterDriver trait: route() → Accept/Pass/Decline/Fail/Defer"
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum RouterResult {
    /// Route accepted — address has been assigned a transport.
    /// C: OK (0)
    Accept {
        /// Transport name to use for delivery.
        transport_name: Option<String>,
        /// Generated host list for remote delivery.
        host_list: Vec<String>,
    },
    /// Pass to next router in chain.
    /// C: PASS (5)
    Pass,
    /// Router declined this address (not applicable).
    /// C: DECLINE (4)
    Decline,
    /// Routing failed permanently.
    /// C: FAIL (2)
    Fail {
        /// Optional failure message.
        message: Option<String>,
    },
    /// Temporary failure — retry later.
    /// C: DEFER (1)
    Defer {
        /// Optional deferral message.
        message: Option<String>,
    },
    /// Internal error.
    /// C: ERROR (3)
    Error {
        /// Error description.
        message: String,
    },
    /// Address was rerouted to a different address (redirect router).
    Rerouted {
        /// The new addresses generated by the redirect.
        new_addresses: Vec<String>,
    },
}

