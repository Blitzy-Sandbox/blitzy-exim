/// Router descriptor flags — replaces C `ri_flags` field in `router_info`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct RouterFlags(u32);

impl RouterFlags {
    /// No flags set.
    pub const NONE: Self = Self(0);
}

