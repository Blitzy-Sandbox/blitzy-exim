// =============================================================================
// exim-drivers/src/router_driver.rs — RouterDriver Trait Definition
// =============================================================================
//
// Defines the `RouterDriver` trait that replaces the C `router_info` struct
// inheritance pattern (structs.h lines 372-387). In C, each router (accept,
// dnslookup, manualroute, redirect, etc.) provides a `router_info` struct with
// a `code()` function pointer that returns an int status. In Rust, this becomes
// a trait with a `route()` method returning a `RouterResult` enum.
//
// Also defines:
//   - `RouterResult`         — Enum replacing C int return codes (OK/DEFER/FAIL/ERROR/DECLINE/PASS)
//   - `RouterInstanceConfig` — Struct replacing C `router_instance` (structs.h lines 299-366)
//   - `RouterFlags`          — Bitflag newtype replacing C `ri_flags` field
//   - `RouterDriverFactory`  — Compile-time registration struct for `inventory::submit!`
//
// This file contains ZERO unsafe code (per AAP §0.7.2).

use crate::DriverError;
use std::any::Any;
use std::collections::HashMap;
use std::fmt;

// =============================================================================
// Router Result Enum
// =============================================================================

/// Result of router evaluation for a single address.
///
/// Maps to C router return codes defined in `exim.h`:
///   - `OK`      (0) — Route accepted, address assigned a transport
///   - `DEFER`   (1) — Temporary failure, retry later
///   - `FAIL`    (2) — Routing failed permanently
///   - `ERROR`   (3) — Internal error
///   - `DECLINE` (4) — Router does not handle this address
///   - `PASS`    (5) — Pass to next router in chain
///
/// Per AAP §0.4.1: "RouterDriver trait: route() → Accept/Pass/Decline/Fail/Defer"
///
/// The `Rerouted` variant has no direct C code equivalent — it represents the
/// case where a redirect router generates new addresses, which in C is encoded
/// via the four `address_item **` output parameters of the router entry point.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum RouterResult {
    /// Route accepted — address has been assigned a transport for delivery.
    ///
    /// C equivalent: return `OK` (0) with the transport pointer set on the
    /// address item and optional host list attached for remote delivery.
    Accept {
        /// Transport name to use for delivery (e.g., "remote_smtp", "local_delivery").
        /// `None` if the transport is inherited from context or set elsewhere.
        transport_name: Option<String>,
        /// Generated host list for remote delivery. Empty for local transports.
        /// Each entry is a host name or IP address string.
        host_list: Vec<String>,
    },

    /// Pass to next router in chain.
    ///
    /// C equivalent: return `PASS` (5). The router evaluated the address but
    /// explicitly passes it to the next router in the chain (different from
    /// `Decline` which means the router is not applicable at all).
    Pass,

    /// Router declined this address — not applicable to this router.
    ///
    /// C equivalent: return `DECLINE` (4). The router determined that it
    /// does not handle this address type/domain and the framework should
    /// try the next router.
    Decline,

    /// Routing failed permanently — the address cannot be delivered.
    ///
    /// C equivalent: return `FAIL` (2). Generates a bounce/DSN message.
    Fail {
        /// Optional human-readable failure message for the bounce.
        message: Option<String>,
    },

    /// Temporary failure — retry later.
    ///
    /// C equivalent: return `DEFER` (1). The address is placed back in the
    /// queue for retry according to the retry rules.
    Defer {
        /// Optional human-readable deferral reason for logging.
        message: Option<String>,
    },

    /// Internal error during routing.
    ///
    /// C equivalent: return `ERROR` (3). Indicates a bug or misconfiguration
    /// rather than a legitimate routing outcome.
    Error {
        /// Error description for logging and diagnostics.
        message: String,
    },

    /// Address was rerouted to one or more new addresses (redirect router).
    ///
    /// No direct C return code — in C, this is encoded as `OK` with new
    /// addresses linked into the `addr_new` chain via the output parameters
    /// of the router entry point. The Rust API makes this explicit.
    Rerouted {
        /// The new addresses generated by the redirect (alias expansion,
        /// filter output, Sieve redirect, etc.).
        new_addresses: Vec<String>,
    },
}

impl RouterResult {
    // =========================================================================
    // Convenience Constructors
    // =========================================================================

    /// Create an `Accept` result for a local transport.
    ///
    /// # Arguments
    /// * `transport_name` — Name of the transport to use (e.g., "local_delivery").
    pub fn accept(transport_name: impl Into<String>) -> Self {
        Self::Accept {
            transport_name: Some(transport_name.into()),
            host_list: Vec::new(),
        }
    }

    /// Create an `Accept` result for a remote transport with a host list.
    ///
    /// # Arguments
    /// * `transport_name` — Name of the transport (e.g., "remote_smtp").
    /// * `hosts` — List of target hosts for remote delivery.
    pub fn accept_remote(transport_name: impl Into<String>, hosts: Vec<String>) -> Self {
        Self::Accept {
            transport_name: Some(transport_name.into()),
            host_list: hosts,
        }
    }

    /// Create a `Fail` result with a message.
    pub fn fail(message: impl Into<String>) -> Self {
        Self::Fail {
            message: Some(message.into()),
        }
    }

    /// Create a `Defer` result with a message.
    pub fn defer(message: impl Into<String>) -> Self {
        Self::Defer {
            message: Some(message.into()),
        }
    }

    /// Create an `Error` result with a message.
    pub fn error(message: impl Into<String>) -> Self {
        Self::Error {
            message: message.into(),
        }
    }

    /// Create a `Rerouted` result with new target addresses.
    pub fn rerouted(addresses: Vec<String>) -> Self {
        Self::Rerouted {
            new_addresses: addresses,
        }
    }

    // =========================================================================
    // C Code Conversion
    // =========================================================================

    /// Convert this result to the corresponding C-style integer return code.
    ///
    /// Mapping:
    ///   `Accept`/`Rerouted` → 0 (OK), `Defer` → 1, `Fail` → 2,
    ///   `Error` → 3, `Decline` → 4, `Pass` → 5
    pub fn to_c_code(&self) -> i32 {
        match self {
            Self::Accept { .. } | Self::Rerouted { .. } => 0,
            Self::Defer { .. } => 1,
            Self::Fail { .. } => 2,
            Self::Error { .. } => 3,
            Self::Decline => 4,
            Self::Pass => 5,
        }
    }

    // =========================================================================
    // Predicate Methods
    // =========================================================================

    /// Returns `true` if this result indicates the address was successfully routed.
    ///
    /// True for `Accept` and `Rerouted` variants.
    pub fn is_accepted(&self) -> bool {
        matches!(self, Self::Accept { .. } | Self::Rerouted { .. })
    }

    /// Returns `true` if this result is a temporary failure (deferral).
    pub fn is_temporary(&self) -> bool {
        matches!(self, Self::Defer { .. })
    }

    /// Returns `true` if this result is a permanent failure or error.
    pub fn is_permanent_failure(&self) -> bool {
        matches!(self, Self::Fail { .. } | Self::Error { .. })
    }

    /// Returns `true` if this result means the router chain should continue
    /// to the next router.
    ///
    /// True for `Pass` and `Decline` variants.
    pub fn should_continue(&self) -> bool {
        matches!(self, Self::Pass | Self::Decline)
    }
}

impl fmt::Display for RouterResult {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Accept {
                transport_name,
                host_list,
            } => {
                write!(f, "ACCEPT")?;
                if let Some(t) = transport_name {
                    write!(f, " transport={t}")?;
                }
                if !host_list.is_empty() {
                    write!(f, " hosts={}", host_list.join(","))?;
                }
                Ok(())
            }
            Self::Pass => write!(f, "PASS"),
            Self::Decline => write!(f, "DECLINE"),
            Self::Fail { message } => {
                write!(f, "FAIL")?;
                if let Some(m) = message {
                    write!(f, ": {m}")?;
                }
                Ok(())
            }
            Self::Defer { message } => {
                write!(f, "DEFER")?;
                if let Some(m) = message {
                    write!(f, ": {m}")?;
                }
                Ok(())
            }
            Self::Error { message } => write!(f, "ERROR: {message}"),
            Self::Rerouted { new_addresses } => {
                write!(f, "REROUTED to {}", new_addresses.join(","))
            }
        }
    }
}

