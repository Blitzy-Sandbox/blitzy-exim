/// Configuration for a single router instance parsed from the Exim config file.
///
/// Replaces the C `router_instance` struct (structs.h lines 299-366). Every
/// field in the C struct has a corresponding Rust field, with C `uschar *`
/// mapped to `Option<String>`, C `BOOL` to `bool`, C `uid_t`/`gid_t` to
/// `u32`, and the opaque `void *options_block` to `Box<dyn Any + Send + Sync>`.
///
/// # C Equivalent
/// ```c
/// typedef struct router_instance {
///   driver_instance drinst;          // name, driver_name, srcfile, srcline
///   uschar *address_data;            // ... through uschar *transport_name
///   BOOL    address_test;            // ... through BOOL dsn_lasthop
///   int     self_code;               // Encoded "self" handling
///   uid_t   uid; gid_t gid;          // Fixed uid/gid
///   host_item *fallback_hostlist;    // Parsed fallback hosts
///   transport_instance *transport;   // Resolved transport (set at runtime)
///   // ... pointers to pass_router, redirect_router, dnssec
/// } router_instance;
/// ```
///
/// The linked-list `next` pointer is replaced by collection-level ownership
/// (a `Vec<RouterInstanceConfig>` in the config subsystem). The `transport`
/// and `pass_router`/`redirect_router` pointers are resolved at runtime by
/// name rather than stored as struct pointers.
#[derive(Debug)]
pub struct RouterInstanceConfig {
    // =========================================================================
    // Base fields (from driver_instance — structs.h lines 142-151)
    // =========================================================================
    /// Instance name from the configuration file.
    ///
    /// This is the user-assigned name from the config (e.g., "dnslookup_router",
    /// "local_user"). In C, this is `drinst.name`.
    pub name: String,

    /// Driver type name (e.g., "accept", "dnslookup", "manualroute", "redirect").
    ///
    /// Matched against registered `RouterDriverFactory::name` values to resolve
    /// the driver implementation. In C, this is `drinst.driver_name`.
    pub driver_name: String,

    /// Configuration source file path for error reporting.
    ///
    /// Set during config file parsing. `None` for programmatically created instances.
    /// In C, this is `drinst.srcfile`.
    pub srcfile: Option<String>,

    /// Configuration source line number for error reporting.
    ///
    /// Set during config file parsing. `None` for programmatically created instances.
    /// In C, this is `drinst.srcline`.
    pub srcline: Option<i32>,

    // =========================================================================
    // Router-specific string fields (structs.h lines 302-328)
    // All map from C `uschar *` → Rust `Option<String>`.
    // =========================================================================
    /// Arbitrary data accessible in conditions and transports via `$address_data`.
    pub address_data: Option<String>,

    /// Error message used when this router cannot route an address.
    /// Overrides the default "Unrouteable address" message.
    pub cannot_route_message: Option<String>,

    /// General condition (expandable string). If set and expands to false/empty,
    /// the router is skipped for this address.
    pub condition: Option<String>,

    /// Working directory set during delivery for the transport.
    pub current_directory: Option<String>,

    /// Debugging output string evaluated when debug mode is active.
    pub debug_string: Option<String>,

    /// Domain list filter — only addresses in matching domains are processed.
    /// Uses Exim domain_list matching syntax.
    pub domains: Option<String>,

    /// Override errors_to address for messages routed by this router.
    pub errors_to: Option<String>,

    /// Expandable string for setting the group ID dynamically.
    pub expand_gid: Option<String>,

    /// Expandable string for setting the user ID dynamically.
    pub expand_uid: Option<String>,

    /// Expandable string for the `more` option.
    pub expand_more: Option<String>,

    /// Expandable string for the `unseen` option.
    pub expand_unseen: Option<String>,

    /// Additional headers to add to messages routed by this router.
    pub extra_headers: Option<String>,

    /// Fallback hosts for remote transports (colon-separated text list).
    /// Used when the primary MX/A lookup yields no usable hosts.
    pub fallback_hosts: Option<String>,

    /// Parsed fallback host list (from `fallback_hosts` or direct configuration).
    /// In C, this is `host_item *fallback_hostlist` — a linked list of host structs.
    pub fallback_hostlist: Vec<String>,

    /// Home directory for use during delivery by the transport.
    pub home_directory: Option<String>,

    /// Host list of target hosts to ignore during routing.
    pub ignore_target_hosts: Option<String>,

    /// Local part list filter — only addresses with matching local parts are processed.
    /// Uses Exim local_part_list matching syntax.
    pub local_parts: Option<String>,

    /// Name of the router to handle addresses that are passed (PASS result).
    /// Resolved at runtime to a `RouterInstanceConfig` reference.
    pub pass_router_name: Option<String>,

    /// Address prefix to strip before routing and re-add after.
    pub prefix: Option<String>,

    /// Name of the router to handle addresses generated by a redirect.
    /// Resolved at runtime to a `RouterInstanceConfig` reference.
    pub redirect_router_name: Option<String>,

    /// Header names to remove from messages routed by this router.
    pub remove_headers: Option<String>,

    /// File existence checks to perform before running this router.
    /// A colon-separated list of file paths; the router is skipped if any
    /// required file is missing.
    pub require_files: Option<String>,

    /// Home directory set while routing (before delivery).
    pub router_home_directory: Option<String>,

    /// Text option for handling "self" reference (when this host is a
    /// listed MX for the destination domain). Encodes actions like "pass",
    /// "fail", "defer", "send", "reroute", "freeze".
    /// Named `self_config` to avoid collision with Rust's `self` keyword.
    pub self_config: Option<String>,

    /// Sender list filter — only messages from matching senders are processed.
    /// Uses Exim sender_list matching syntax.
    pub senders: Option<String>,

    /// Address suffix to strip before routing and re-add after.
    pub suffix: Option<String>,

    /// Expandable string for IP address translation (fudgery).
    pub translate_ip_address: Option<String>,

    /// Transport name to assign to routed addresses.
    /// Resolved at runtime to a transport instance.
    pub transport_name: Option<String>,

    // =========================================================================
    // Boolean flags (structs.h lines 330-354)
    // All map from C `BOOL` → Rust `bool`.
    // =========================================================================
    /// Use this router when testing addresses (via `-bt` flag).
    pub address_test: bool,

    /// Use this router when processing SMTP EXPN commands.
    pub expn: bool,

    /// If `true`, the local part is treated case-sensitively (not lowercased).
    pub caseful_local_part: bool,

    /// If `true`, check that the local part corresponds to a local system user
    /// before running the router. Sets `$local_user_*` expansion variables.
    pub check_local_user: bool,

    /// Disable logging for this router's actions.
    pub disable_logging: bool,

    /// Fail recipient verification if the recipient matches this router.
    pub fail_verify_recipient: bool,

    /// Fail sender verification if the sender matches this router.
    pub fail_verify_sender: bool,

    /// Flag indicating that `gid` has been explicitly set.
    pub gid_set: bool,

    /// If `true`, call `initgroups()` when setting the uid for delivery.
    pub initgroups: bool,

    /// If `true`, log this delivery as a local delivery even if it uses
    /// a remote transport.
    pub log_as_local: bool,

    /// If `false`, do not try more routers if this one fails (returns FAIL).
    /// Default is `true` — continue to next router on failure.
    pub more: bool,

    /// Treat timeout-caused DEFER results as FAIL.
    pub pass_on_timeout: bool,

    /// The address prefix is optional (may or may not be present).
    pub prefix_optional: bool,

    /// If `false`, skip this router if an ancestor address already used it.
    /// Prevents infinite routing loops.
    pub repeat_use: bool,

    /// Include the local part in the retry key for deferred addresses.
    pub retry_use_local_part: bool,

    /// If `true`, copy routing decisions for addresses in the same domain.
    pub same_domain_copy_routing: bool,

    /// If `true`, rewrite message headers when routing creates a local delivery.
    pub self_rewrite: bool,

    /// The address suffix is optional (may or may not be present).
    pub suffix_optional: bool,

    /// Skip this router if we are not verifying (only run during verification).
    pub verify_only: bool,

    /// Use this router when verifying recipients.
    pub verify_recipient: bool,

    /// Use this router when verifying senders.
    pub verify_sender: bool,

    /// Flag indicating that `uid` has been explicitly set.
    pub uid_set: bool,

    /// If `true`, continue processing subsequent routers even after this one
    /// accepts the address. Used for blind-carbon-copy or logging routers.
    pub unseen: bool,

    /// If `true`, mark this router as a DSN last-hop — DSN notifications
    /// are generated here rather than passed further.
    pub dsn_lasthop: bool,

    // =========================================================================
    // Numeric fields (structs.h lines 356-358)
    // =========================================================================
    /// Encoded version of the `self` handling option.
    ///
    /// Maps the `self_config` text to an integer code used internally:
    ///   - 0 = freeze, 1 = defer, 2 = fail, 3 = send, 4 = reroute, 5 = pass
    pub self_code: i32,

    /// Fixed user ID for delivery. Only used when `uid_set` is `true`.
    pub uid: u32,

    /// Fixed group ID for delivery. Only used when `gid_set` is `true`.
    pub gid: u32,

    // =========================================================================
    // Variable settings (structs.h line 347 — between BOOL flags)
    // =========================================================================
    /// Variable = value setting list. A semicolon-separated list of
    /// `variable = value` assignments that are applied when this router runs.
    pub set: Option<String>,

    // =========================================================================
    // Driver-specific options (replaces C `void *options_block`)
    // =========================================================================
    /// Opaque driver-specific options block.
    ///
    /// Each router driver type defines its own options struct (e.g.,
    /// `dnslookup_router_options_block`, `redirect_router_options_block`).
    /// The framework stores it as a type-erased `Box<dyn Any + Send + Sync>`
    /// and the driver implementation downcasts it to the concrete type.
    ///
    /// In C, this was `void *options_block` on `driver_instance`.
    pub options: Box<dyn Any + Send + Sync>,

    /// Driver-specific options as a simple string key-value map.
    ///
    /// Populated by the config parser from the raw private option lines.
    /// Each entry maps an option name (e.g., `"data"`, `"file"`) to its
    /// string value (everything after the `=` sign, trimmed).
    /// This provides a type-agnostic way for driver implementations to
    /// access their private options without needing to downcast the
    /// `options` field.
    pub private_options_map: HashMap<String, String>,
}

impl RouterInstanceConfig {
    /// Create a new `RouterInstanceConfig` with the given instance name and driver name.
    ///
    /// All optional string fields default to `None`, boolean flags default to
    /// values matching the C `readconf.c` initialization pattern:
    ///   - `address_test`, `expn`, `more`, `repeat_use` → `true`
    ///   - `retry_use_local_part`, `verify_recipient`, `verify_sender` → `true`
    ///   - All other booleans → `false`
    ///   - Numeric fields → `0`
    ///   - `options` → `Box::new(())` (empty unit type)
    pub fn new(name: impl Into<String>, driver_name: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            driver_name: driver_name.into(),
            srcfile: None,
            srcline: None,
            address_data: None,
            cannot_route_message: None,
            condition: None,
            current_directory: None,
            debug_string: None,
            domains: None,
            errors_to: None,
            expand_gid: None,
            expand_uid: None,
            expand_more: None,
            expand_unseen: None,
            extra_headers: None,
            fallback_hosts: None,
            fallback_hostlist: Vec::new(),
            home_directory: None,
            ignore_target_hosts: None,
            local_parts: None,
            pass_router_name: None,
            prefix: None,
            redirect_router_name: None,
            remove_headers: None,
            require_files: None,
            router_home_directory: None,
            self_config: None,
            senders: None,
            suffix: None,
            translate_ip_address: None,
            transport_name: None,
            // Boolean defaults matching C readconf.c initialization
            address_test: true,
            expn: true,
            caseful_local_part: false,
            check_local_user: false,
            disable_logging: false,
            fail_verify_recipient: false,
            fail_verify_sender: false,
            gid_set: false,
            initgroups: false,
            log_as_local: false,
            more: true,
            pass_on_timeout: false,
            prefix_optional: false,
            repeat_use: true,
            retry_use_local_part: true,
            same_domain_copy_routing: false,
            self_rewrite: false,
            suffix_optional: false,
            verify_only: false,
            verify_recipient: true,
            verify_sender: true,
            uid_set: false,
            unseen: false,
            dsn_lasthop: false,
            self_code: 0,
            uid: 0,
            gid: 0,
            set: None,
            options: Box::new(()),
            private_options_map: HashMap::new(),
        }
    }

    /// Create a new `RouterInstanceConfig` with configuration source information.
    ///
    /// Used during configuration file parsing when the source file and line
    /// number are known.
    pub fn with_source(
        name: impl Into<String>,
        driver_name: impl Into<String>,
        srcfile: impl Into<String>,
        srcline: i32,
    ) -> Self {
        let mut config = Self::new(name, driver_name);
        config.srcfile = Some(srcfile.into());
        config.srcline = Some(srcline);
        config
    }

    /// Format the configuration source location for error messages.
    ///
    /// Returns a string like `"filename:42"` or `"<unknown>"` if no source
    /// information is available.
    pub fn source_location(&self) -> String {
        match (&self.srcfile, self.srcline) {
            (Some(file), Some(line)) => format!("{file}:{line}"),
            (Some(file), None) => file.clone(),
            _ => "<unknown>".to_string(),
        }
    }
}

impl fmt::Display for RouterInstanceConfig {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "router {}({})", self.name, self.driver_name)
    }
}

