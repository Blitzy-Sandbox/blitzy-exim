/// Configuration for a router instance, from Exim config file.
///
/// Replaces C `router_instance` struct (structs.h lines 299-366).
#[derive(Debug)]
pub struct RouterInstanceConfig {
    // Base fields (from driver_instance)
    /// Instance name from config.
    pub name: String,
    /// Driver name (e.g., "accept", "dnslookup", "manualroute").
    pub driver_name: String,
    /// Config source file for error reporting.
    pub srcfile: Option<String>,
    /// Config source line for error reporting.
    pub srcline: Option<i32>,

    // Router-specific generic fields
    /// Arbitrary data accessible in conditions.
    pub address_data: Option<String>,
    /// Message when routing fails.
    pub cannot_route_message: Option<String>,
    /// General condition (expandable string).
    pub condition: Option<String>,
    /// Current directory for use during delivery.
    pub current_directory: Option<String>,
    /// Debugging output string.
    pub debug_string: Option<String>,
    /// Specific domains (domain_list).
    pub domains: Option<String>,
    /// Errors address.
    pub errors_to: Option<String>,
    /// Expanded gid string.
    pub expand_gid: Option<String>,
    /// Expanded uid string.
    pub expand_uid: Option<String>,
    /// Expanded "more" string.
    pub expand_more: Option<String>,
    /// Expanded "unseen" string.
    pub expand_unseen: Option<String>,
    /// Additional headers.
    pub extra_headers: Option<String>,
    /// Fallback hosts for remote transports (text list).
    pub fallback_hosts: Option<String>,
    /// Parsed fallback host list.
    pub fallback_hostlist: Vec<String>,
    /// Home directory for use during delivery.
    pub home_directory: Option<String>,
    /// Target hosts to ignore.
    pub ignore_target_hosts: Option<String>,
    /// Specific local parts (local_part_list).
    pub local_parts: Option<String>,
    /// Router name for passed addresses.
    pub pass_router_name: Option<String>,
    /// Address prefix.
    pub prefix: Option<String>,
    /// Router name for generated (redirected) addresses.
    pub redirect_router_name: Option<String>,
    /// Headers to remove.
    pub remove_headers: Option<String>,
    /// File checks before router is run.
    pub require_files: Option<String>,
    /// Home directory for use while routing.
    pub router_home_directory: Option<String>,
    /// Text option for handling self reference.
    pub self_config: Option<String>,
    /// Specific senders (sender_list).
    pub senders: Option<String>,
    /// Address suffix.
    pub suffix: Option<String>,
    /// IP address translation fudgery.
    pub translate_ip_address: Option<String>,
    /// Transport name.
    pub transport_name: Option<String>,

    // Boolean flags
    /// Use this router when testing addresses.
    pub address_test: bool,
    /// Use this router when processing EXPN.
    pub expn: bool,
    /// TRUE => don't lowercase local part.
    pub caseful_local_part: bool,
    /// TRUE => check local user.
    pub check_local_user: bool,
    /// Disable logging for this router.
    pub disable_logging: bool,
    /// Fail verify if recipient matches this router.
    pub fail_verify_recipient: bool,
    /// Fail verify if sender matches this router.
    pub fail_verify_sender: bool,
    /// Flag to indicate gid is set.
    pub gid_set: bool,
    /// TRUE if initgroups is required.
    pub initgroups: bool,
    /// TRUE logs as a local delivery.
    pub log_as_local: bool,
    /// If FALSE, do no more if this one fails.
    pub more: bool,
    /// Treat timeout DEFERs as fails.
    pub pass_on_timeout: bool,
    /// Prefix is optional.
    pub prefix_optional: bool,
    /// If FALSE, skip if ancestor used it.
    pub repeat_use: bool,
    /// Use local part in retry key.
    pub retry_use_local_part: bool,
    /// TRUE => copy routing for same domain.
    pub same_domain_copy_routing: bool,
    /// TRUE to rewrite headers if making local.
    pub self_rewrite: bool,
    /// Suffix is optional.
    pub suffix_optional: bool,
    /// Skip this router if not verifying.
    pub verify_only: bool,
    /// Use this router when verifying a recipient.
    pub verify_recipient: bool,
    /// Use this router when verifying a sender.
    pub verify_sender: bool,
    /// Flag to indicate uid is set.
    pub uid_set: bool,
    /// If TRUE carry on, even after success.
    pub unseen: bool,
    /// If TRUE, this router is a DSN endpoint.
    pub dsn_lasthop: bool,

    // Numeric fields
    /// Encoded version of "self" handling.
    pub self_code: i32,
    /// Fixed uid value.
    pub uid: u32,
    /// Fixed gid value.
    pub gid: u32,

    /// Variable = value setting list.
    pub set: Option<String>,

    /// Driver-specific options (opaque to framework).
    pub options: Box<dyn std::any::Any + Send + Sync>,
}

