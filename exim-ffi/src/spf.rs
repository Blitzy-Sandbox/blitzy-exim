/// Safe wrapper around an SPF server context (`SPF_server_t`).
///
/// The SPF server manages DNS resolution configuration and caching for SPF
/// queries. It is typically created once per Exim process lifetime (or per
/// connection batch) and reused across multiple SPF requests.
///
/// # Lifecycle
///
/// The server is automatically freed via `SPF_server_free` when dropped.
/// Creating a new request via [`new_request`](SpfServer::new_request) does
/// NOT transfer ownership of the server — the server must outlive all
/// requests derived from it.
///
/// # Thread Safety
///
/// This type is intentionally `!Send` and `!Sync` because the underlying
/// libspf2 `SPF_server_t` is not thread-safe.
pub struct SpfServer {
    server: *mut ffi::SPF_server_t,
}

impl SpfServer {
    /// Create a new SPF server with DNS caching enabled.
    ///
    /// This creates a server using `SPF_DNS_CACHE` as the DNS resolver type,
    /// matching the Exim C code pattern: `SPF_server_new(SPF_DNS_CACHE, 0)`.
    /// The debug level is set to 0 (no debug output).
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if `SPF_server_new` returns a null pointer,
    /// indicating a memory allocation failure or library initialization error.
    pub fn new() -> Result<Self, SpfError> {
        // unsafe justification: calling SPF_server_new to create an SPF server
        // context with DNS caching enabled. The function allocates a new
        // SPF_server_t and returns null on failure, which we check immediately.
        // SPF_DNS_CACHE is a compile-time constant from libspf2.
        let server = unsafe { ffi::SPF_server_new(ffi::SPF_server_dnstype_enum_SPF_DNS_CACHE, 0) };
        if server.is_null() {
            return Err(SpfError::new(
                "SPF_server_new returned null — failed to initialize SPF server context",
            ));
        }
        Ok(Self { server })
    }

    /// Create a new SPF request associated with this server.
    ///
    /// The returned [`SpfRequest`] uses this server's DNS resolver and cache
    /// configuration. Multiple concurrent requests can share the same server.
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if `SPF_request_new` returns a null pointer.
    pub fn new_request(&self) -> Result<SpfRequest, SpfError> {
        // unsafe justification: calling SPF_request_new to allocate and
        // initialize an SPF request associated with this server. The server
        // pointer was validated non-null during SpfServer construction.
        // Returns null on allocation failure.
        let request = unsafe { ffi::SPF_request_new(self.server) };
        if request.is_null() {
            return Err(SpfError::new(
                "SPF_request_new returned null — failed to create SPF request",
            ));
        }
        Ok(SpfRequest { request })
    }

    /// Get the libspf2 library version as `(major, minor, patch)`.
    ///
    /// Calls `SPF_get_lib_version` to retrieve the compile-time version
    /// numbers of the linked libspf2 library (e.g., `(1, 2, 10)`).
    pub fn lib_version() -> (i32, i32, i32) {
        let mut major: c_int = 0;
        let mut minor: c_int = 0;
        let mut patch: c_int = 0;
        // unsafe justification: calling SPF_get_lib_version to retrieve the
        // compile-time library version numbers. The function writes to the
        // three provided pointer locations, which are valid stack variables
        // with deterministic lifetimes.
        unsafe {
            ffi::SPF_get_lib_version(&mut major, &mut minor, &mut patch);
        }
        (major as i32, minor as i32, patch as i32)
    }
}

impl Drop for SpfServer {
    fn drop(&mut self) {
        if !self.server.is_null() {
            // unsafe justification: calling SPF_server_free to release all
            // memory and resources associated with the SPF server context.
            // The pointer was validated non-null during construction and is
            // set to null after freeing to prevent double-free.
            unsafe {
                ffi::SPF_server_free(self.server);
            }
            self.server = ptr::null_mut();
        }
    }
}

