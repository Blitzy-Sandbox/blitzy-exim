/// SPF processing modes controlling how SPF evaluation is performed.
///
/// Maps to the C constants in `src/src/miscmods/spf.h` lines 31–40:
/// - `SPF_PROCESS_NORMAL  = 0`
/// - `SPF_PROCESS_GUESS   = 1`
/// - `SPF_PROCESS_FALLBACK = 2`
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
#[repr(i32)]
pub enum SpfProcessMode {
    /// Normal SPF processing using standard DNS lookups and the domain's
    /// published SPF record.
    Normal = 0,
    /// Guess mode — applies best-guess SPF records for domains that do not
    /// publish their own SPF record.
    Guess = 1,
    /// Fallback mode — applies a configured fallback SPF record when the
    /// domain's record cannot be retrieved.
    Fallback = 2,
}

