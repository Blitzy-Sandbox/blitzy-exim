//! Safe wrappers around libspf2 for SPF (Sender Policy Framework) validation.
//!
//! Provides managed SPF server, request, and response objects that wrap
//! the libspf2 C API for checking SPF records against connecting IP addresses.
//!
//! Supports custom DNS resolver hooks via the Exim DNS subsystem integration.
//!
//! # Safety
//!
//! All `unsafe` blocks in this module are confined to the `exim-ffi` crate,
//! which is the ONLY crate permitted to contain `unsafe` code per AAP §0.7.2.
//! Each `unsafe` block has an inline justification comment explaining its
//! necessity. The safe public API prevents null pointer dereference and
//! properly manages the lifecycle of libspf2 objects through RAII (`Drop`).
//!
//! # Source Context
//!
//! This module replaces the C implementation in:
//! - `src/src/miscmods/spf.c` (627 lines — SPF evaluation + DNS bridge)
//! - `src/src/miscmods/spf.h` (41 lines — SPF type definitions + constants)
//! - `src/src/miscmods/spf_api.h` (36 lines — result codes + function table)

use std::error::Error as StdError;
use std::ffi::{CStr, CString};
use std::fmt;
use std::ptr;

use libc::c_int;

