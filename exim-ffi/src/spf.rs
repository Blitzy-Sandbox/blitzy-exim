/// Safe wrapper around an SPF request (`SPF_request_t`).
///
/// An SPF request encapsulates the parameters needed for a single SPF
/// evaluation: the connecting client's IP address, HELO/EHLO domain, and
/// envelope sender (MAIL FROM) address.
///
/// # Usage Flow
///
/// ```ignore
/// let server = SpfServer::new()?;
/// let mut req = server.new_request()?;
/// req.set_ipv4("192.168.1.100")?;
/// req.set_helo_domain("mail.example.com")?;
/// req.set_env_from("sender@example.com")?;
/// let response = req.query_mailfrom()?;
/// println!("SPF result: {}", response.result());
/// ```
///
/// # Lifecycle
///
/// The request is automatically freed via `SPF_request_free` when dropped.
///
/// # Thread Safety
///
/// This type is intentionally `!Send` and `!Sync` because the underlying
/// libspf2 `SPF_request_t` is not thread-safe.
pub struct SpfRequest {
    request: *mut ffi::SPF_request_t,
}

impl SpfRequest {
    /// Set the client IP address (IPv4).
    ///
    /// The IP address string should be in standard dotted-decimal notation
    /// (e.g., `"192.168.1.1"` or `"10.0.0.42"`).
    ///
    /// This corresponds to the C call:
    /// `SPF_request_set_ipv4_str(spf_request, CS ip_address)`
    /// from `src/src/miscmods/spf.c` line ~380.
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the IP address string contains an interior
    /// null byte, or if `SPF_request_set_ipv4_str` returns a non-success
    /// error code (e.g., invalid format).
    pub fn set_ipv4(&mut self, ip: &str) -> Result<(), SpfError> {
        let c_ip = to_cstring(ip, "IPv4 address")?;
        // Dispatch: SetIpv4 — sets the connecting client's IPv4 address.
        let rc = match spf_ffi(SpfFfi::SetIpv4 {
            req: self.request,
            ip: c_ip.as_ptr(),
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != 0 {
            return Err(SpfError::new(format!(
                "SPF_request_set_ipv4_str failed for '{}' (error code {})",
                ip, rc
            )));
        }
        Ok(())
    }

    /// Set the client IP address (IPv6).
    ///
    /// The IP address string should be in standard colon-hex notation
    /// (e.g., `"2001:db8::1"` or `"::ffff:192.168.1.1"`).
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the IP address string contains an interior
    /// null byte, or if `SPF_request_set_ipv6_str` returns a non-success
    /// error code.
    pub fn set_ipv6(&mut self, ip: &str) -> Result<(), SpfError> {
        let c_ip = to_cstring(ip, "IPv6 address")?;
        // Dispatch: SetIpv6 — sets the connecting client's IPv6 address.
        let rc = match spf_ffi(SpfFfi::SetIpv6 {
            req: self.request,
            ip: c_ip.as_ptr(),
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != 0 {
            return Err(SpfError::new(format!(
                "SPF_request_set_ipv6_str failed for '{}' (error code {})",
                ip, rc
            )));
        }
        Ok(())
    }

    /// Set the HELO/EHLO domain.
    ///
    /// The domain string is the argument presented by the connecting client
    /// in the SMTP HELO or EHLO command (e.g., `"mail.example.com"`).
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the domain string contains an interior null
    /// byte, or if `SPF_request_set_helo_dom` returns a non-success error
    /// code.
    pub fn set_helo_domain(&mut self, domain: &str) -> Result<(), SpfError> {
        let c_domain = to_cstring(domain, "HELO domain")?;
        // Dispatch: SetHeloDom — sets the HELO domain for SPF evaluation.
        let rc = match spf_ffi(SpfFfi::SetHeloDom {
            req: self.request,
            dom: c_domain.as_ptr(),
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != 0 {
            return Err(SpfError::new(format!(
                "SPF_request_set_helo_dom failed for '{}' (error code {})",
                domain, rc
            )));
        }
        Ok(())
    }

    /// Set the envelope sender (MAIL FROM) address.
    ///
    /// Note: The underlying `SPF_request_set_env_from` returns `int` (not
    /// `SPF_errcode_t`), where non-zero indicates an error. This matches
    /// the Exim C source pattern at line ~434:
    /// `if (SPF_request_set_env_from(spf_request, CS mailaddr))`.
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the sender contains an interior null byte,
    /// or if `SPF_request_set_env_from` returns a non-zero value.
    pub fn set_env_from(&mut self, sender: &str) -> Result<(), SpfError> {
        let c_sender = to_cstring(sender, "envelope sender")?;
        // Dispatch: SetEnvFrom — sets the envelope sender.
        let rc = match spf_ffi(SpfFfi::SetEnvFrom {
            req: self.request,
            sender: c_sender.as_ptr(),
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != 0 {
            return Err(SpfError::new(format!(
                "SPF_request_set_env_from failed for '{}' (error code {})",
                sender, rc
            )));
        }
        Ok(())
    }

    /// Query the SPF record for the MAIL FROM sender.
    ///
    /// Performs the full SPF lookup and validation, including DNS resolution
    /// of TXT records, SPF record parsing, mechanism evaluation, and result
    /// determination. Returns a response object containing the result code
    /// and human-readable explanation.
    ///
    /// This corresponds to the C call:
    /// `SPF_request_query_mailfrom(spf_request, &spf_response)`
    /// from `src/src/miscmods/spf.c` line ~440.
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the query encounters a fatal error or if the
    /// response pointer is null after the query completes.
    pub fn query_mailfrom(&self) -> Result<SpfResponse, SpfError> {
        let mut response: *mut ffi::SPF_response_t = ptr::null_mut();
        // Dispatch: QueryMailfrom — performs SPF lookup and validation.
        let rc = match spf_ffi(SpfFfi::QueryMailfrom {
            req: self.request,
            resp: &mut response,
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if response.is_null() {
            return Err(SpfError::new(format!(
                "SPF_request_query_mailfrom returned null response (error code {})",
                rc
            )));
        }
        Ok(SpfResponse { response })
    }

    /// Query the SPF record for a RCPT TO recipient (secondary validation).
    ///
    /// The `rcpt_to` parameter specifies the recipient address for the
    /// per-recipient SPF check. This is used for SPF validation scoped to
    /// individual recipients rather than the global MAIL FROM.
    ///
    /// Note: The C function signature for `SPF_request_query_rcptto` includes
    /// an additional `const char *rcpt_to` parameter compared to
    /// `SPF_request_query_mailfrom`.
    ///
    /// # Errors
    ///
    /// Returns [`SpfError`] if the query fails, `rcpt_to` contains an
    /// interior null byte, or the response pointer is null after the query.
    pub fn query_rcptto(&self, rcpt_to: &str) -> Result<SpfResponse, SpfError> {
        let c_rcpt = to_cstring(rcpt_to, "RCPT TO address")?;
        let mut response: *mut ffi::SPF_response_t = ptr::null_mut();
        // Dispatch: QueryRcptto — per-recipient SPF validation.
        let rc = match spf_ffi(SpfFfi::QueryRcptto {
            req: self.request,
            resp: &mut response,
            rcpt: c_rcpt.as_ptr(),
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if response.is_null() {
            return Err(SpfError::new(format!(
                "SPF_request_query_rcptto returned null response for '{}' (error code {})",
                rcpt_to, rc
            )));
        }
        Ok(SpfResponse { response })
    }
}

impl Drop for SpfRequest {
    fn drop(&mut self) {
        if !self.request.is_null() {
            // Dispatch: RequestFree — releases SPF request resources.
            spf_ffi(SpfFfi::RequestFree { req: self.request });
            self.request = ptr::null_mut();
        }
    }
}

// ---------------------------------------------------------------------------
// SpfResponse — safe wrapper around SPF_response_t
// ---------------------------------------------------------------------------

/// Safe wrapper around an SPF response (`SPF_response_t`).
///
/// Contains the result of an SPF evaluation, including the result code
/// (pass/fail/softfail/etc.), a human-readable result string, and a reason
/// explanation string.
///
/// # Lifecycle
///
/// The response is automatically freed via `SPF_response_free` when dropped.
/// Responses are created by [`SpfRequest::query_mailfrom`] or
/// [`SpfRequest::query_rcptto`] and must NOT outlive the originating request.
///
/// # Thread Safety
///
/// This type is intentionally `!Send` and `!Sync` because the underlying
/// libspf2 `SPF_response_t` is not thread-safe.
pub struct SpfResponse {
    response: *mut ffi::SPF_response_t,
}

impl SpfResponse {
    /// Get the SPF result code.
    ///
    /// Calls `SPF_response_result` to retrieve the numeric result and maps
    /// it to the [`SpfResult`] enum. If the result code is outside the known
    /// range (0–7), returns [`SpfResult::Invalid`] as a safe fallback.
    pub fn result(&self) -> SpfResult {
        // Dispatch: ResponseResult — retrieves the SPF result code.
        let code = match spf_ffi(SpfFfi::ResponseResult {
            resp: self.response,
        }) {
            SpfFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        SpfResult::from_code(code as i32).unwrap_or(SpfResult::Invalid)
    }

    /// Get the human-readable result string from libspf2.
    ///
    /// Calls `SPF_response_result` followed by `SPF_strresult` to convert
    /// the numeric result to the library's canonical string representation
    /// (e.g., `"pass"`, `"fail"`, `"softfail"`, `"temperror"`).
    ///
    /// Returns `"unknown"` if the library returns a null string pointer.
    pub fn result_str(&self) -> String {
        // Dispatch: ResultStr — converts result code to string.
        match spf_ffi(SpfFfi::ResultStr {
            resp: self.response,
        }) {
            SpfFfiResult::Str(s) => s,
            _ => unreachable!(),
        }
    }

    /// Get the reason string for the SPF result.
    ///
    /// Calls `SPF_response_reason` followed by `SPF_strreason` to get a
    /// human-readable explanation of why the SPF result was determined
    /// (e.g., `"mechanism"`, `"default"`, `"local-policy"`).
    ///
    /// Returns `"unknown"` if the library returns a null string pointer.
    pub fn reason_str(&self) -> String {
        // Dispatch: ReasonStr — converts reason code to string.
        match spf_ffi(SpfFfi::ReasonStr {
            resp: self.response,
        }) {
            SpfFfiResult::Str(s) => s,
            _ => unreachable!(),
        }
    }
}

impl Drop for SpfResponse {
    fn drop(&mut self) {
        if !self.response.is_null() {
            // Dispatch: ResponseFree — releases SPF response resources.
            spf_ffi(SpfFfi::ResponseFree {
                resp: self.response,
            });
            self.response = ptr::null_mut();
        }
    }
}

