/// FFI operation descriptor for libspf2.
#[allow(dead_code)] // Justification: some variants are only used in specific code paths
enum SpfFfi {
    ServerNew {
        dns_type: libc::c_int,
        debug: libc::c_int,
    },
    /// SP1: Create SPF server using a caller-provided DNS resolver stack.
    ///
    /// Calls `SPF_server_new_dns(dns, debug)`. The provided DNS server is
    /// borrowed for the lifetime of the returned `SPF_server_t`; the SPF
    /// server does NOT take ownership unless the DNS server's `destroy`
    /// callback is non-null (we always set it to NULL so the Rust side
    /// owns the lifecycle).
    ServerNewDns {
        dns: *mut ffi::SPF_dns_server_t,
        debug: libc::c_int,
    },
    /// SP2: Set the receiving domain (used for %{r}, %{d}, %{h} macros).
    ///
    /// Calls `SPF_server_set_rec_dom(sp, dom)`. The `dom` pointer must
    /// reference a NUL-terminated C string that remains valid for the
    /// duration of the call.
    ServerSetRecDom {
        server: *mut ffi::SPF_server_t,
        dom: *const libc::c_char,
    },
    RequestNew {
        server: *mut ffi::SPF_server_t,
    },
    LibVersion {
        major: *mut c_int,
        minor: *mut c_int,
        patch: *mut c_int,
    },
    ServerFree {
        server: *mut ffi::SPF_server_t,
    },
    SetIpv4 {
        req: *mut ffi::SPF_request_t,
        ip: *const libc::c_char,
    },
    SetIpv6 {
        req: *mut ffi::SPF_request_t,
        ip: *const libc::c_char,
    },
    SetHeloDom {
        req: *mut ffi::SPF_request_t,
        dom: *const libc::c_char,
    },
    SetEnvFrom {
        req: *mut ffi::SPF_request_t,
        sender: *const libc::c_char,
    },
    QueryMailfrom {
        req: *mut ffi::SPF_request_t,
        resp: *mut *mut ffi::SPF_response_t,
    },
    QueryRcptto {
        req: *mut ffi::SPF_request_t,
        resp: *mut *mut ffi::SPF_response_t,
        rcpt: *const libc::c_char,
    },
    RequestFree {
        req: *mut ffi::SPF_request_t,
    },
    ResponseResult {
        resp: *mut ffi::SPF_response_t,
    },
    ResultStr {
        resp: *mut ffi::SPF_response_t,
    },
    ReasonStr {
        resp: *mut ffi::SPF_response_t,
    },
    ResponseFree {
        resp: *mut ffi::SPF_response_t,
    },
    /// SP1: Allocate an `SPF_dns_rr_t` with the given herrno/ttl/rr_type.
    ///
    /// Calls `SPF_dns_rr_new_init(server, domain, rr_type, ttl, herrno)`.
    /// Used by the Rust DNS trampoline when building a response on behalf
    /// of the caller's DNS hook.
    DnsRrNewInit {
        server: *mut ffi::SPF_dns_server_t,
        domain: *const libc::c_char,
        rr_type: ffi::ns_type,
        ttl: libc::c_int,
        herrno: ffi::SPF_dns_stat_t,
    },
    /// SP1: Resize the `idx`-th entry of `spfrr->rr[]` to `len` bytes.
    ///
    /// Calls `SPF_dns_rr_buf_realloc(spfrr, idx, len)`. Used by the DNS
    /// trampoline to grow the rr buffer before memcpy'ing record data.
    DnsRrBufRealloc {
        spfrr: *mut ffi::SPF_dns_rr_t,
        idx: libc::c_int,
        len: libc::size_t,
    },
    /// SP1: Free an `SPF_dns_rr_t` allocated by the DNS layer.
    ///
    /// Calls `SPF_dns_rr_free(spfrr)`. This is ordinarily invoked by
    /// libspf2 itself when the DNS cache is flushed; the trampoline
    /// does NOT call this directly (ownership transfers to libspf2
    /// once the pointer is returned).
    DnsRrFree {
        spfrr: *mut ffi::SPF_dns_rr_t,
    },
}

/// Result of an SPF FFI dispatch operation.
enum SpfFfiResult {
    Ptr(*mut libc::c_void),
    Code(libc::c_int),
    Str(String),
    Done,
}

/// Single-point-of-entry for all libspf2 FFI calls.
fn spf_ffi(op: SpfFfi) -> SpfFfiResult {
    // SAFETY: All unsafe libspf2 FFI calls consolidated here. Each variant's
    // safety is documented at the call site constructing the SpfFfi variant.
    unsafe {
        match op {
            SpfFfi::ServerNew { dns_type, debug } => {
                SpfFfiResult::Ptr(ffi::SPF_server_new(dns_type as u32, debug) as *mut libc::c_void)
            }
            SpfFfi::ServerNewDns { dns, debug } => {
                SpfFfiResult::Ptr(ffi::SPF_server_new_dns(dns, debug) as *mut libc::c_void)
            }
            SpfFfi::ServerSetRecDom { server, dom } => {
                SpfFfiResult::Code(ffi::SPF_server_set_rec_dom(server, dom) as libc::c_int)
            }
            SpfFfi::RequestNew { server } => {
                SpfFfiResult::Ptr(ffi::SPF_request_new(server) as *mut libc::c_void)
            }
            SpfFfi::LibVersion {
                major,
                minor,
                patch,
            } => {
                ffi::SPF_get_lib_version(major, minor, patch);
                SpfFfiResult::Done
            }
            SpfFfi::ServerFree { server } => {
                ffi::SPF_server_free(server);
                SpfFfiResult::Done
            }
            SpfFfi::SetIpv4 { req, ip } => {
                SpfFfiResult::Code(ffi::SPF_request_set_ipv4_str(req, ip) as libc::c_int)
            }
            SpfFfi::SetIpv6 { req, ip } => {
                SpfFfiResult::Code(ffi::SPF_request_set_ipv6_str(req, ip) as libc::c_int)
            }
            SpfFfi::SetHeloDom { req, dom } => {
                SpfFfiResult::Code(ffi::SPF_request_set_helo_dom(req, dom) as libc::c_int)
            }
            SpfFfi::SetEnvFrom { req, sender } => {
                SpfFfiResult::Code(ffi::SPF_request_set_env_from(req, sender) as libc::c_int)
            }
            SpfFfi::QueryMailfrom { req, resp } => {
                SpfFfiResult::Code(ffi::SPF_request_query_mailfrom(req, resp) as libc::c_int)
            }
            SpfFfi::QueryRcptto { req, resp, rcpt } => {
                SpfFfiResult::Code(ffi::SPF_request_query_rcptto(req, resp, rcpt) as libc::c_int)
            }
            SpfFfi::RequestFree { req } => {
                ffi::SPF_request_free(req);
                SpfFfiResult::Done
            }
            SpfFfi::ResponseResult { resp } => {
                SpfFfiResult::Code(ffi::SPF_response_result(resp) as libc::c_int)
            }
            SpfFfi::ResultStr { resp } => {
                let code = ffi::SPF_response_result(resp);
                let c_str = ffi::SPF_strresult(code);
                if c_str.is_null() {
                    SpfFfiResult::Str("unknown".to_string())
                } else {
                    SpfFfiResult::Str(CStr::from_ptr(c_str).to_string_lossy().into_owned())
                }
            }
            SpfFfi::ReasonStr { resp } => {
                let reason = ffi::SPF_response_reason(resp);
                let c_str = ffi::SPF_strreason(reason);
                if c_str.is_null() {
                    SpfFfiResult::Str("unknown".to_string())
                } else {
                    SpfFfiResult::Str(CStr::from_ptr(c_str).to_string_lossy().into_owned())
                }
            }
            SpfFfi::ResponseFree { resp } => {
                ffi::SPF_response_free(resp);
                SpfFfiResult::Done
            }
            SpfFfi::DnsRrNewInit {
                server,
                domain,
                rr_type,
                ttl,
                herrno,
            } => SpfFfiResult::Ptr(
                ffi::SPF_dns_rr_new_init(server, domain, rr_type, ttl, herrno) as *mut libc::c_void,
            ),
            SpfFfi::DnsRrBufRealloc { spfrr, idx, len } => {
                SpfFfiResult::Code(ffi::SPF_dns_rr_buf_realloc(spfrr, idx, len) as libc::c_int)
            }
            SpfFfi::DnsRrFree { spfrr } => {
                ffi::SPF_dns_rr_free(spfrr);
                SpfFfiResult::Done
            }
        }
    }
}

