    fn spf_server_lib_version() {
        let (major, minor, patch) = SpfServer::lib_version();
        // libspf2 version should be reasonable (1.x.x range)
        assert!(major >= 0);
        assert!(minor >= 0);
        assert!(patch >= 0);
    }
