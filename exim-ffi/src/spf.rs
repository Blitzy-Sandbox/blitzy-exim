/// Callback type for custom DNS resolution.
///
/// This allows the Exim DNS subsystem to be plugged in as the DNS resolver
/// for libspf2, rather than using libspf2's built-in resolver. The function
/// receives a domain name and DNS resource record type, and returns matching
/// records or an error.
///
/// The `rr_type` parameter uses standard DNS RR type values:
/// - `1`  — A record (IPv4 address)
/// - `15` — MX record (mail exchange)
/// - `16` — TXT record (text, containing SPF data)
/// - `28` — AAAA record (IPv6 address)
/// - `33` — SRV record (service locator)
///
/// # Source Context
///
/// This replaces the `SPF_dns_exim_lookup` extern "C" callback function
/// defined in `src/src/miscmods/spf.c` lines 66–160, which bridges
/// libspf2's DNS queries to Exim's internal DNS resolution subsystem.
pub type DnsLookupFn = Box<dyn Fn(&str, u16) -> Result<Vec<DnsRecord>, SpfError> + Send + Sync>;

/// A DNS record returned by the custom resolver.
///
/// Represents a single resource record with its type and raw data payload,
/// suitable for passing back through the libspf2 DNS callback interface.
#[derive(Debug, Clone)]
pub struct DnsRecord {
    /// DNS resource record type (e.g., 1=A, 15=MX, 16=TXT, 28=AAAA).
    pub rr_type: u16,
    /// Raw record data bytes as they would appear in a DNS response.
    pub data: Vec<u8>,
}

