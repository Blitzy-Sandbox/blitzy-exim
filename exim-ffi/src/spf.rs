/// Error type for SPF operations.
///
/// Wraps error messages from libspf2 API calls and provides full
/// `std::error::Error` integration for idiomatic Rust error handling.
#[derive(Debug, Clone)]
pub struct SpfError {
    message: String,
}

impl SpfError {
    /// Create a new SPF error with the given descriptive message.
    pub fn new(msg: impl Into<String>) -> Self {
        Self {
            message: msg.into(),
        }
    }
}

impl fmt::Display for SpfError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "SPF error: {}", self.message)
    }
}

impl StdError for SpfError {}

