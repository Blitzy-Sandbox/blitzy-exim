//! Safe wrappers around the GNU SASL library (libgsasl).
//!
//! Provides managed GSASL context and session handles for SASL authentication,
//! supporting multiple mechanisms (PLAIN, LOGIN, CRAM-MD5, SCRAM-SHA-1/256, etc.)
//!
//! Handles version-dependent API differences between GSASL 1.x and 2.x via
//! build-time `cfg` attributes emitted by `build.rs` (see `detect_gsasl_version`).
//!
//! # Safety Policy
//!
//! All `unsafe` blocks in this module are documented with inline justification
//! comments. The raw C pointers (`*mut Gsasl`, `*mut Gsasl_session`) are wrapped
//! in RAII structs whose `Drop` implementations call the appropriate C cleanup
//! functions (`gsasl_done`, `gsasl_finish`).
//!
//! # Example
//!
//! ```no_run
//! use exim_ffi::gsasl::{GsaslContext, GsaslProperty, StepResult};
//!
//! let mut ctx = GsaslContext::new().expect("failed to init GSASL");
//! let mut session = ctx.server_start("PLAIN").expect("failed to start session");
//! session.property_set(GsaslProperty::Service, "smtp").unwrap();
//! ```

use std::ffi::{CStr, CString};
use std::fmt;
use std::ptr;

// ---------------------------------------------------------------------------
// Raw FFI bindings generated by bindgen (via build.rs → gsasl_bindings.rs).
// ---------------------------------------------------------------------------

#[allow(
    non_upper_case_globals,
    non_camel_case_types,
    non_snake_case,
    dead_code,
    // Justification: bindgen-generated FFI bindings must preserve the C naming
    // conventions from <gsasl.h>. Suppressing these lints is standard practice
    // for machine-generated code and does not hide any logic errors.
    clippy::upper_case_acronyms,
    clippy::unreadable_literal
)]
mod ffi {
    include!(concat!(env!("OUT_DIR"), "/gsasl_bindings.rs"));
}

// ---------------------------------------------------------------------------
// Constants — GSASL return codes used for control flow.
// ---------------------------------------------------------------------------
// Note: bindgen renders the Gsasl_rc C enum as a set of
// `Gsasl_rc_GSASL_*` constants of type `c_uint`. We re-export the
// handful we need as `c_int` for comparison with the `c_int` returns
// from the GSASL API functions.

/// GSASL success return code (always 0).
const GSASL_RC_OK: libc::c_int = 0;

/// GSASL "needs more data" return code (always 1).
const GSASL_RC_NEEDS_MORE: libc::c_int = 1;

/// GSASL "no callback installed" return code (always 51).
const GSASL_RC_NO_CALLBACK: libc::c_int = 51;

/// GSASL "authentication error" return code (always 31).
const GSASL_RC_AUTHENTICATION_ERROR: libc::c_int = 31;

/// GSASL "unknown mechanism" return code (always 2).
const GSASL_RC_UNKNOWN_MECHANISM: libc::c_int = 2;

/// GSASL "malloc error" return code (always 7).
const GSASL_RC_MALLOC_ERROR: libc::c_int = 7;

// ---------------------------------------------------------------------------
// GsaslError — Typed error wrapping a GSASL numeric error code.
// ---------------------------------------------------------------------------

/// Error type representing a GSASL library error.
///
/// Wraps a numeric GSASL error code together with the human-readable error
/// message obtained from `gsasl_strerror()`.
#[derive(Debug, Clone)]
pub struct GsaslError {
    /// The raw GSASL error code (e.g. `GSASL_AUTHENTICATION_ERROR`).
    code: i32,
    /// Human-readable description from `gsasl_strerror`.
    message: String,
}

impl GsaslError {
    /// Create a `GsaslError` from a raw GSASL return code.
    ///
    /// Calls `gsasl_strerror` to obtain the human-readable error description.
    pub fn from_code(code: i32) -> Self {
        // SAFETY: `gsasl_strerror` is a pure lookup function that returns a
        // pointer to a statically-allocated NUL-terminated C string for any
        // integer argument. The returned pointer is valid for the lifetime of
        // the library and is never NULL for known codes. We immediately copy
        // the string into an owned Rust `String` via `CStr::from_ptr`.
        let message = unsafe {
            let c_msg = ffi::gsasl_strerror(code as libc::c_int);
            if c_msg.is_null() {
                format!("GSASL error {code}")
            } else {
                CStr::from_ptr(c_msg).to_string_lossy().into_owned()
            }
        };
        Self { code, message }
    }

    /// Return the raw GSASL numeric error code.
    pub fn code(&self) -> i32 {
        self.code
    }
}

impl fmt::Display for GsaslError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "GSASL error {}: {}", self.code, self.message)
    }
}

impl std::error::Error for GsaslError {}

// ---------------------------------------------------------------------------
// StepResult — Outcome of a single SASL exchange step.
// ---------------------------------------------------------------------------

/// Result of a single SASL authentication step.
///
/// A SASL exchange consists of one or more round-trips. Each call to
/// [`GsaslSession::step`] returns either `Continue` (more steps required) or
/// `Done` (authentication decision reached).
#[derive(Debug)]
pub enum StepResult {
    /// More steps are needed. The contained bytes should be sent to the peer.
    Continue(Vec<u8>),
    /// Authentication exchange is complete. The contained bytes are the final
    /// token to send to the peer (may be empty).
    Done(Vec<u8>),
}

