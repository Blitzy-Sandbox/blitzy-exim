//! Safe wrappers around the embedded Perl interpreter (libperl).
//!
//! Provides a managed [`PerlInterpreter`] that supports:
//! - Interpreter startup with configured options
//! - Adding Perl code blocks for evaluation
//! - Calling Perl functions with arguments and capturing return values
//!
//! This wraps the Perl C API: `perl_alloc`, `perl_construct`, `perl_parse`,
//! `perl_run`, `Perl_eval_pv`, `Perl_call_pv`, `perl_destruct`, `perl_free`.
//!
//! Perl embedding is inherently FFI — there is NO viable Rust-native
//! replacement (AAP §0.6.2).
//!
//! # Safety Model
//!
//! All `unsafe` blocks are confined to this module within the `exim-ffi` crate
//! (the ONLY crate permitted to contain unsafe code per AAP §0.7.2). Each
//! `unsafe` block has an inline justification comment. The raw
//! `*mut ffi::PerlInterpreter` pointer is wrapped in an RAII struct whose
//! [`Drop`] implementation calls `perl_destruct` and `perl_free`.
//!
//! # Source Context
//!
//! This module replaces the C implementation in:
//! - `src/src/miscmods/perl.c` (345 lines — embedded Perl integration)
//! - `src/src/miscmods/perl_api.h` (17 lines — function table entry numbers)

use std::ffi::CString;
use std::fmt;
use std::ptr;

