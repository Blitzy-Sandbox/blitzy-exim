/// XS initialization callback passed to `perl_parse`.
///
/// Called by the Perl runtime during interpreter startup to register built-in
/// XS functions. Registers DynaLoader to enable dynamic Perl module loading.
///
/// # Source Context
///
/// Mirrors the `xs_init` function in `perl.c` lines 170-179. The C version
/// registers DynaLoader plus four Exim-specific XS functions (`Exim::expand_string`,
/// `Exim::debug_write`, `Exim::log_write`, `Exim::dns_lookup`). We register
/// only DynaLoader here because the Exim-specific functions require access to
/// Exim internals and are registered at a higher level in the `exim-miscmods`
/// crate, not in the FFI layer.
///
/// # Safety
///
/// This function is called by the Perl runtime during `perl_parse`. The
/// `my_perl` parameter is a valid interpreter being initialized. The function
/// is `unsafe extern "C"` because it is a C callback invoked by the Perl
/// library.
unsafe extern "C" fn xs_init_callback(my_perl: *mut ffi::PerlInterpreter) {
    // Register DynaLoader bootstrap function for XS module support.
    // "DynaLoader::boot_DynaLoader" is the standard name that Perl's
    // DynaLoader module expects to find for its bootstrap entry point.
    let name = c"DynaLoader::boot_DynaLoader";
    let file = c"exim-ffi/src/perl.rs";
    ffi::Perl_newXS(
        my_perl,
        name.as_ptr(),
        Some(ffi::boot_DynaLoader),
        file.as_ptr(),
    );
}

