/// Register the DynaLoader boot function needed for XS module loading.
///
/// This is a crate-internal helper that can be used to register DynaLoader
/// on an existing interpreter, for example when setting up a custom
/// `xs_init` callback that needs to register additional XS functions
/// alongside DynaLoader.
///
/// In normal usage, DynaLoader is automatically registered via the
/// [`xs_init_callback`] during [`PerlInterpreter::startup()`].
///
/// # Errors
///
/// Returns [`PerlError`] if the interpreter pointer is null.
#[allow(dead_code)]
// Justification: This is a crate-internal helper for registering DynaLoader on
// an existing interpreter from other FFI modules. It is not currently called
// within exim-ffi but is provided as a reusable building block for future XS
// registration patterns (e.g., when a custom xs_init needs to register both
// DynaLoader and Exim-specific XS functions).
pub(crate) fn register_dynaloader(interp: &mut PerlInterpreter) -> Result<(), PerlError> {
    if interp.interp.is_null() {
        return Err(PerlError::new(
            "cannot register DynaLoader: interpreter is null",
        ));
    }

    // Safety justification: calling Perl_newXS to register the DynaLoader
    // boot function on a valid interpreter. The interpreter pointer is
    // guaranteed non-null by the check above. The name and file byte
    // literals are compile-time constant null-terminated strings.
    // boot_DynaLoader is a symbol provided by the Perl DynaLoader
    // shared object at link time.
    unsafe {
        let name = c"DynaLoader::boot_DynaLoader";
        let file = c"exim-ffi/src/perl.rs";
        ffi::Perl_newXS(
            interp.interp,
            name.as_ptr(),
            Some(ffi::boot_DynaLoader),
            file.as_ptr(),
        );
    }

    Ok(())
}

