/// Escape a string for use as a Perl single-quoted string literal.
///
/// In Perl, single-quoted strings only require escaping of two characters:
/// - `\` (backslash) → `\\`
/// - `'` (single quote) → `\'`
///
/// All other characters (including `$`, `@`, `%`, `{`, `}`, newlines, etc.)
/// are treated as literal characters inside single quotes.
///
/// The result is wrapped in single quotes: `'escaped_content'`.
///
/// This is used by [`PerlInterpreter::call_function()`] to safely pass
/// string arguments to Perl functions via `Perl_eval_pv`.
///
/// # Examples
///
/// ```ignore
/// assert_eq!(escape_perl_single_quote("hello"), "'hello'");
/// assert_eq!(escape_perl_single_quote("it's"), "'it\\'s'");
/// assert_eq!(escape_perl_single_quote("a\\b"), "'a\\\\b'");
/// assert_eq!(escape_perl_single_quote("$var"), "'$var'");
/// ```
fn escape_perl_single_quote(s: &str) -> String {
    // Pre-allocate with room for the two surrounding quotes plus a small
    // margin for escape sequences.
    let mut result = String::with_capacity(s.len() + 4);
    result.push('\'');
    for ch in s.chars() {
        match ch {
            '\'' => result.push_str("\\'"),
            '\\' => result.push_str("\\\\"),
            _ => result.push(ch),
        }
    }
    result.push('\'');
    result
}

