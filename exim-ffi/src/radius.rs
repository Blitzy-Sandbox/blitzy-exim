/// RADIUS authentication client handle.
///
/// Wraps the `rc_handle` from freeradiusclient/radiusclient in an RAII struct.
/// Configuration is loaded during construction via `rc_read_config()` and
/// `rc_read_dictionary()`.
#[cfg(radius_lib_radiusclient)]
pub struct RadiusClient {
    /// Raw pointer to the radiusclient handle. Initialized by `rc_read_config()`.
    /// The original C code (src/src/miscmods/radius.c) does not explicitly free
    /// this handle because authentication runs in short-lived forked child processes.
    /// We preserve this behavior in Drop — see the Drop implementation for details.
    handle: *mut ffi::rc_handle,
}

#[cfg(radius_lib_radiusclient)]
impl RadiusClient {
    /// Open a RADIUS client connection using the specified configuration file.
    ///
    /// Calls `rc_read_config()` to parse the RADIUS client configuration and
    /// `rc_read_dictionary()` to load the RADIUS attribute dictionary.
    ///
    /// # Arguments
    ///
    /// * `config_file` — Path to the radiusclient configuration file
    ///
    /// # Errors
    ///
    /// Returns [`RadiusError`] if:
    /// - `config_file` contains a null byte
    /// - `rc_read_config()` returns a null pointer (configuration parse failure)
    /// - `rc_read_dictionary()` fails to load the RADIUS dictionary
    pub fn new(config_file: &str) -> Result<Self, RadiusError> {
        let c_config = CString::new(config_file)
            .map_err(|_| RadiusError::new("config file path contains interior null byte"))?;

        let c_dictionary_key = CString::new("dictionary")
            .map_err(|_| RadiusError::new("internal error: dictionary key conversion"))?;

        // Radiusclient initialisation via centralised dispatch.
        let h = match rclient_ffi(RclientFfi::ReadConfig {
            path: c_config.as_ptr(),
        }) {
            RclientFfiResult::Handle(h) => h,
            _ => unreachable!(),
        };
        if h.is_null() {
            return Err(RadiusError::new(format!(
                "RADIUS: can't open {}",
                config_file
            )));
        }

        let dict_path = match rclient_ffi(RclientFfi::ConfStr {
            handle: h,
            key: c_dictionary_key.as_ptr(),
        }) {
            RclientFfiResult::CPtr(p) => p,
            _ => unreachable!(),
        };
        if dict_path.is_null() {
            return Err(RadiusError::new(
                "RADIUS: dictionary path not found in configuration",
            ));
        }

        let rc = match rclient_ffi(RclientFfi::ReadDictionary {
            handle: h,
            dict_path,
        }) {
            RclientFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != 0 {
            return Err(RadiusError::new("RADIUS: can't read dictionary"));
        }

        Ok(Self { handle: h })
    }

    /// Authenticate a user with the given username and password.
    ///
    /// Builds a RADIUS Access-Request by adding User-Name, User-Password, and
    /// Service-Type attribute-value pairs, then calls `rc_auth()` to perform
    /// the authentication exchange. The response is mapped to [`RadiusAuthResult`].
    ///
    /// # Arguments
    ///
    /// * `user` — The username to authenticate
    /// * `password` — The user's password
    ///
    /// # Errors
    ///
    /// Returns [`RadiusError`] if string conversion fails or if attribute-value
    /// pair construction fails.
    pub fn authenticate(
        &self,
        user: &str,
        password: &str,
    ) -> Result<RadiusAuthResult, RadiusError> {
        let c_user = CString::new(user)
            .map_err(|_| RadiusError::new("username contains interior null byte"))?;
        let c_password = CString::new(password)
            .map_err(|_| RadiusError::new("password contains interior null byte"))?;

        let mut send: *mut ffi::VALUE_PAIR = std::ptr::null_mut();
        let mut received: *mut ffi::VALUE_PAIR = std::ptr::null_mut();
        let mut msg = [0u8; 4096];
        let service: libc::c_uint = ffi::rc_service_type_PW_AUTHENTICATE_ONLY as libc::c_uint;

        // AVP construction and authentication via centralised dispatch.
        let rc = match rclient_ffi(RclientFfi::AvpairAdd {
            handle: self.handle,
            send: &mut send,
            attr: ffi::rc_attr_id_PW_USER_NAME as libc::c_int,
            value: c_user.as_ptr() as *const libc::c_void,
            len: c_user.as_bytes().len() as libc::c_int,
            vendor: 0,
        }) {
            RclientFfiResult::VpPtr(p) => p,
            _ => unreachable!(),
        };
        if rc.is_null() {
            return Err(RadiusError::new("RADIUS: add user name failed"));
        }

        let rc = match rclient_ffi(RclientFfi::AvpairAdd {
            handle: self.handle,
            send: &mut send,
            attr: ffi::rc_attr_id_PW_USER_PASSWORD as libc::c_int,
            value: c_password.as_ptr() as *const libc::c_void,
            len: c_password.as_bytes().len() as libc::c_int,
            vendor: 0,
        }) {
            RclientFfiResult::VpPtr(p) => p,
            _ => unreachable!(),
        };
        if rc.is_null() {
            return Err(RadiusError::new("RADIUS: add password failed"));
        }

        let rc = match rclient_ffi(RclientFfi::AvpairAdd {
            handle: self.handle,
            send: &mut send,
            attr: ffi::rc_attr_id_PW_SERVICE_TYPE as libc::c_int,
            value: &service as *const libc::c_uint as *const libc::c_void,
            len: 0,
            vendor: 0,
        }) {
            RclientFfiResult::VpPtr(p) => p,
            _ => unreachable!(),
        };
        if rc.is_null() {
            return Err(RadiusError::new("RADIUS: add service type failed"));
        }

        let result = match rclient_ffi(RclientFfi::Auth {
            handle: self.handle,
            port: 0,
            send,
            received: &mut received,
            msg: msg.as_mut_ptr() as *mut libc::c_char,
        }) {
            RclientFfiResult::Code(c) => c,
            _ => unreachable!(),
        };

        match result {
            x if x == rc_result_codes::OK_RC => Ok(RadiusAuthResult::Ok),
            x if x == rc_result_codes::REJECT_RC => Ok(RadiusAuthResult::Fail),
            x if x == rc_result_codes::ERROR_RC => Ok(RadiusAuthResult::Fail),
            x if x == rc_result_codes::TIMEOUT_RC => Err(RadiusError::new("RADIUS: timed out")),
            x if x == rc_result_codes::BADRESP_RC => Ok(RadiusAuthResult::BadResponse(result)),
            other => Ok(RadiusAuthResult::BadResponse(other)),
        }
    }
}

#[cfg(radius_lib_radiusclient)]
impl Drop for RadiusClient {
    fn drop(&mut self) {
        // The original C code (src/src/miscmods/radius.c) does not explicitly free
        // the rc_handle because auth_call_radius() runs in a short-lived forked child
        // process that exits after handling the SMTP connection. The OS reclaims all
        // memory on process exit.
        //
        // In the Rust implementation, RadiusClient is used via the top-level
        // authenticate() convenience function which creates and immediately drops
        // the client, matching the C one-shot pattern. No explicit cleanup function
        // is called because:
        //   1. The original C code does not call one
        //   2. The rc_destroy() function may not exist in all radiusclient variants
        //   3. The handle is typically used in a forked child process
        //
        // If a future radiusclient version provides a universal cleanup function,
        // it should be added here.
        let _ = self.handle; // Suppress unused field warning in drop
    }
}

