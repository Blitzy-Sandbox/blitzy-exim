/// Perform RADIUS authentication.
///
/// This is a convenience function that combines [`RadiusClient::new()`] and
/// [`RadiusClient::authenticate()`] into a single call, matching the one-shot
/// pattern of the original C `auth_call_radius()` function.
///
/// # Arguments
///
/// * `config_file` — Path to the RADIUS configuration file
/// * `user` — The username to authenticate
/// * `password` — The user's password
///
/// # Returns
///
/// - `Ok(RadiusAuthResult::Ok)` — Authentication succeeded
/// - `Ok(RadiusAuthResult::Fail)` — Authentication rejected
/// - `Ok(RadiusAuthResult::BadResponse(code))` — Unexpected response from server
/// - `Err(RadiusError)` — Error during the authentication process
///
/// # Examples
///
/// ```ignore
/// use exim_ffi::radius::{authenticate, RadiusAuthResult};
///
/// match authenticate("/etc/radius.conf", "user@example.com", "s3cret") {
///     Ok(RadiusAuthResult::Ok) => println!("Authenticated!"),
///     Ok(RadiusAuthResult::Fail) => println!("Bad credentials"),
///     Ok(RadiusAuthResult::BadResponse(code)) => eprintln!("Unexpected: {}", code),
///     Err(e) => eprintln!("Error: {}", e),
///     _ => {}
/// }
/// ```
pub fn authenticate(
    config_file: &str,
    user: &str,
    password: &str,
) -> Result<RadiusAuthResult, RadiusError> {
    let client = RadiusClient::new(config_file)?;
    client.authenticate(user, password)
}

