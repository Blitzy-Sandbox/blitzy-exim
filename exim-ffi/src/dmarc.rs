//! Safe wrappers around libopendmarc for DMARC (Domain-based Message
//! Authentication, Reporting, and Conformance) policy evaluation.
//!
//! This module is feature-gated behind `#[cfg(feature = "ffi-dmarc")]` and is
//! the **ONLY** location in the workspace where `unsafe` calls to libopendmarc
//! are permitted, per AAP §0.7.2.
//!
//! The C DMARC module (`src/src/miscmods/dmarc.c`) calls 20+ libopendmarc
//! functions. All of these are wrapped in safe Rust APIs exposed by this module.
//!
//! # Safety
//!
//! All `unsafe` blocks in this module are confined to the `exim-ffi` crate,
//! which is the ONLY crate permitted to contain `unsafe` code per AAP §0.7.2.
//! Each `unsafe` block has an inline justification comment explaining its
//! necessity. The safe public API prevents null pointer dereference and properly
//! manages the lifecycle of libopendmarc objects through RAII (`Drop`).
//!
//! # Source Context
//!
//! This module replaces the C implementation in:
//! - `src/src/miscmods/dmarc.c` (477 lines — DMARC evaluation + libopendmarc calls)
//! - `src/src/miscmods/dmarc.h` (144 lines — DMARC types, constants, extern decls)
//! - `src/src/miscmods/dmarc_api.h` (16 lines — function table entry numbers)

