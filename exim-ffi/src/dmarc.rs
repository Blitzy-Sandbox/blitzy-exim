// Internal helper: safe CString construction
// ---------------------------------------------------------------------------

/// Convert a Rust `&str` to a [`CString`], mapping interior NUL byte errors
/// to [`DmarcFfiError`] with a descriptive context message.
fn to_cstring(s: &str, context: &str) -> Result<CString, DmarcFfiError> {
    CString::new(s).map_err(|_| {
        DmarcFfiError::new(
            DMARC_PARSE_ERROR_BAD_VALUE as i32,
            format!(
                "{context} contains an interior null byte and cannot be passed to libopendmarc"
            ),
        )
    })
}

/// Check an OPENDMARC_STATUS_T return code and convert to Result.
///
/// Returns `Ok(())` when the status equals `DMARC_PARSE_OKAY`, otherwise
/// returns `Err(DmarcFfiError)` enriched with the libopendmarc status string.
fn check_status(status: c_int, context: &str) -> Result<(), DmarcFfiError> {
    if status == DMARC_PARSE_OKAY as c_int {
        Ok(())
    } else {
        Err(DmarcFfiError::from_status(status, context))
    }
}

