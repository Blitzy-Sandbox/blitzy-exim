/// Error type for libopendmarc FFI operations.
///
/// Wraps an OPENDMARC_STATUS_T status code together with a human-readable
/// description of the failure.
///
/// # Fields
///
/// - `status` — The numeric `OPENDMARC_STATUS_T` code returned by the C
///   library function. See the `DMARC_PARSE_*`, `DMARC_DNS_*`, and
///   `DMARC_POLICY_*` constants for values.
/// - `message` — A descriptive error message indicating what operation failed
///   and (where available) the libopendmarc status string.
#[derive(Debug, Clone)]
pub struct DmarcFfiError {
    /// Numeric status code from libopendmarc (OPENDMARC_STATUS_T).
    pub status: i32,
    /// Human-readable error description.
    pub message: String,
}

impl DmarcFfiError {
    /// Create a new DMARC FFI error with the given status code and message.
    fn new(status: i32, msg: impl Into<String>) -> Self {
        Self {
            status,
            message: msg.into(),
        }
    }

    /// Create a DMARC FFI error enriched with the libopendmarc status string.
    ///
    /// Calls `opendmarc_policy_status_to_str` to obtain the canonical name
    /// for the status code and appends it to the message.
    fn from_status(status: i32, context: &str) -> Self {
        let status_str = status_to_string(status);
        Self {
            status,
            message: format!("{context}: {status_str} (code {status})"),
        }
    }
}

impl fmt::Display for DmarcFfiError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "DMARC FFI error ({}): {}", self.status, self.message)
    }
}

impl StdError for DmarcFfiError {}

