/// Result of a PAM authentication attempt.
///
/// Maps PAM return codes to a three-state enum that distinguishes between
/// successful authentication, expected authentication failures (wrong
/// password, unknown user, expired account), and unexpected errors.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PamAuthResult {
    /// Authentication succeeded — `pam_authenticate` and `pam_acct_mgmt`
    /// both returned `PAM_SUCCESS`.
    Ok,
    /// Authentication failed due to expected reasons: bad credentials
    /// (`PAM_AUTH_ERR`), unknown user (`PAM_USER_UNKNOWN`), or expired
    /// account (`PAM_ACCT_EXPIRED`).
    Fail,
    /// An unexpected error occurred during PAM processing.
    Error(PamError),
}

// ---------------------------------------------------------------------------
// High-level authenticate function
// ---------------------------------------------------------------------------

/// Perform PAM authentication with the given credentials.
///
/// This is the primary entry point for PAM authentication, equivalent to
/// `auth_call_pam()` in the original C implementation
/// (`src/src/miscmods/pam.c`).
///
/// The function:
/// 1. Opens a PAM session via `pam_start`
/// 2. Calls `pam_authenticate` (which invokes the conversation callback to
///    get credentials)
/// 3. On success, calls `pam_acct_mgmt` to check account restrictions
/// 4. Maps PAM error codes to [`PamAuthResult`]
/// 5. Automatically closes the PAM session via [`PamHandle::drop`]
///
/// # Arguments
///
/// * `service` — The PAM service name (typically `"exim"`)
/// * `user` — The username to authenticate
/// * `credentials` — Credential strings provided to PAM when it requests
///   them via the conversation callback
///
/// # Returns
///
/// * [`PamAuthResult::Ok`] — authentication and account validation both
///   succeeded
/// * [`PamAuthResult::Fail`] — authentication failed (wrong password,
///   unknown user, expired account)
/// * [`PamAuthResult::Error`] — an unexpected PAM error occurred
pub fn authenticate(service: &str, user: &str, credentials: &[&str]) -> PamAuthResult {
    // Reject empty usernames — PAM doesn't support them and would prompt
    // for one, causing potential misinterpretation.
    if user.is_empty() {
        return PamAuthResult::Fail;
    }

    // Create PAM handle with conversation data containing the credentials.
    let handle = match PamHandle::start_with_credentials(service, user, credentials) {
        Result::Ok(h) => h,
        Err(e) => return PamAuthResult::Error(e),
    };

    // Perform authentication — this triggers the conversation callback.
    if let Err(e) = handle.authenticate(PAM_SILENT) {
        return classify_pam_error(e);
    }

    // Check account validity (time-of-day, expired, disabled, etc.).
    if let Err(e) = handle.acct_mgmt(PAM_SILENT) {
        return classify_pam_error(e);
    }

    // PamHandle::drop will call pam_end automatically.
    PamAuthResult::Ok
}

/// Classify a PAM error into `Fail` (expected auth failure) or `Error`
/// (unexpected).
///
/// Known authentication failure codes are mapped to [`PamAuthResult::Fail`]:
/// - `PAM_AUTH_ERR` — incorrect credentials
/// - `PAM_USER_UNKNOWN` — user does not exist
/// - `PAM_ACCT_EXPIRED` — account has expired
///
/// All other error codes are mapped to [`PamAuthResult::Error`].
fn classify_pam_error(err: PamError) -> PamAuthResult {
    let code = err.code() as u32;
    if code == ffi::PAM_AUTH_ERR || code == ffi::PAM_USER_UNKNOWN || code == ffi::PAM_ACCT_EXPIRED {
        PamAuthResult::Fail
    } else {
        PamAuthResult::Error(err)
    }
}

