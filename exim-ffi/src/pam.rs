/// PAM flag: suppress all messages during authentication.
///
/// Passed to [`PamHandle::authenticate`] and [`PamHandle::acct_mgmt`] to
/// prevent the PAM library from generating any messages. Equivalent to
/// `PAM_SILENT` in C.
pub const PAM_SILENT: i32 = ffi::PAM_SILENT as i32;

// ---------------------------------------------------------------------------
// PamError
// ---------------------------------------------------------------------------

/// PAM error codes mapped to Rust.
///
/// Wraps a raw PAM error code (`c_int`) returned by PAM library functions
/// (`pam_start`, `pam_authenticate`, `pam_acct_mgmt`, etc.) into a typed
/// error that implements [`std::error::Error`] and [`fmt::Display`].
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct PamError {
    code: i32,
}

impl PamError {
    /// Create a new `PamError` from a raw PAM error code.
    pub fn new(code: i32) -> Self {
        Self { code }
    }

    /// Return the raw PAM error code.
    pub fn code(&self) -> i32 {
        self.code
    }
}

impl fmt::Display for PamError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "PAM error code {}", self.code)
    }
}

impl std::error::Error for PamError {}

