//! Safe wrappers around libpam for PAM authentication.
//!
//! This module wraps the PAM conversation-based authentication API,
//! providing a safe Rust interface that converts C error codes to
//! `Result<T, PamError>`.
//!
//! # Safety
//!
//! All `unsafe` code in this module is confined to FFI calls to libpam
//! functions. Every `unsafe` block has an inline justification comment
//! explaining why it is necessary and why the operation is sound. The
//! module exposes only safe public APIs to consumers.
//!
//! # Feature Gate
//!
//! This module is compiled only when the `ffi-pam` feature is enabled,
//! replacing the C preprocessor `SUPPORT_PAM` conditional from
//! `src/src/miscmods/pam.c`.
//!
//! # PAM Conversation Model
//!
//! PAM authenticates users through a *conversation* callback: the PAM
//! library sends prompts (password requests, informational messages) and
//! the application responds with credentials. This module implements the
//! conversation callback as an `extern "C"` function that draws responses
//! from a [`PamConversationData`] struct passed via `pam_conv.appdata_ptr`.
//!
//! Responses are allocated with `libc::malloc` / `libc::strdup` because
//! PAM takes ownership of response memory and calls `free()` on it.

use std::ffi::{CStr, CString};
use std::fmt;
use std::ptr;

// Include raw bindings generated by build.rs via bindgen.
//
// The PAM C header naming conventions (PAM_SUCCESS, pam_handle_t, etc.)
// must be preserved in the generated bindings.
#[allow(
    non_upper_case_globals,
    non_camel_case_types,
    non_snake_case,
    dead_code
)]
// Justification: bindgen-generated FFI bindings must preserve C naming
// conventions. These four lints fire on every generated typedef, constant,
// and function name because C headers use SCREAMING_CASE constants,
// snake_case_t type suffixes, and non-Rust-idiomatic struct field names.
// Suppressing them on the entire private `ffi` module is the standard
// pattern for bindgen output.
mod ffi {
    include!(concat!(env!("OUT_DIR"), "/pam_bindings.rs"));
}

