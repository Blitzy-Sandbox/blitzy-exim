/// Safe wrapper around a PAM authentication session.
///
/// Manages the lifecycle of a `pam_handle_t`, ensuring `pam_end` is always
/// called when the handle is dropped. The handle also owns the conversation
/// data, keeping it alive for the entire PAM session.
///
/// # RAII Guarantee
///
/// `pam_end()` is called in the [`Drop`] implementation, ensuring PAM
/// resources are always released even if authentication fails or panics
/// occur.
pub struct PamHandle {
    /// Raw PAM handle returned by `pam_start`. Null indicates an
    /// invalid / consumed handle.
    handle: *mut ffi::pam_handle_t,

    /// Heap-allocated conversation data containing credentials. PAM stores
    /// a raw pointer to this via `pam_conv.appdata_ptr`, so it must remain
    /// at a stable address. Created with [`Box::into_raw`] in
    /// [`start_with_credentials`](PamHandle::start_with_credentials) and
    /// reclaimed with [`Box::from_raw`] in [`Drop`].
    conv_data_ptr: *mut PamConversationData,
}

impl PamHandle {
    /// Start a new PAM authentication session.
    ///
    /// Creates a PAM handle for the given service name and username. The
    /// conversation callback is configured internally to provide empty
    /// responses. Use the module-level [`authenticate`] function for
    /// credential-based authentication.
    ///
    /// # Arguments
    ///
    /// * `service` — The PAM service name (typically `"exim"`)
    /// * `user` — The username to authenticate
    ///
    /// # Errors
    ///
    /// Returns [`PamError`] if `pam_start` fails or if the service / user
    /// strings contain interior null bytes.
    pub fn start(service: &str, user: &str) -> Result<Self, PamError> {
        Self::start_with_credentials(service, user, &[])
    }

    /// Internal constructor that sets up PAM with credential-bearing
    /// conversation data.
    ///
    /// The conversation data is heap-allocated via `Box::into_raw` so that
    /// the raw pointer passed to `pam_conv.appdata_ptr` remains valid for
    /// the entire lifetime of the returned `PamHandle`.
    fn start_with_credentials(
        service: &str,
        user: &str,
        credentials: &[&str],
    ) -> Result<Self, PamError> {
        let c_service = CString::new(service).map_err(|_| PamError::new(-1))?;
        let c_user = CString::new(user).map_err(|_| PamError::new(-1))?;

        // Heap-allocate conversation data and convert to a raw pointer.
        // We use Box::into_raw so the allocation survives and remains at a
        // stable address. We reclaim it in Drop via Box::from_raw.
        let conv_data = Box::new(PamConversationData {
            responses: credentials.iter().map(|s| (*s).to_string()).collect(),
            current_index: 0,
        });
        let conv_data_ptr = Box::into_raw(conv_data);

        // The pam_conv struct references our conversation callback and
        // points to the heap-allocated conversation data.
        let pamc = ffi::pam_conv {
            conv: Some(pam_conversation),
            appdata_ptr: conv_data_ptr as *mut libc::c_void,
        };

        let mut handle: *mut ffi::pam_handle_t = ptr::null_mut();

        // PAM session initialisation via centralised dispatch.
        let rc = match pam_ffi(PamFfi::Start {
            service: c_service.as_ptr(),
            user: c_user.as_ptr(),
            conv: &pamc as *const ffi::pam_conv,
            handle_out: &mut handle,
        }) {
            PamFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if rc != ffi::PAM_SUCCESS as i32 || handle.is_null() {
            pam_ffi(PamFfi::ReclaimConvData { ptr: conv_data_ptr });
            return Err(PamError::new(rc));
        }

        Ok(PamHandle {
            handle,
            conv_data_ptr,
        })
    }

    /// Authenticate the user via PAM.
    ///
    /// Calls `pam_authenticate()` which triggers the conversation callback
    /// to provide credentials. The `flags` parameter controls PAM behaviour
    /// (typically [`PAM_SILENT`]).
    ///
    /// # Errors
    ///
    /// Returns [`PamError`] with the PAM error code if authentication fails.
    pub fn authenticate(&self, flags: i32) -> Result<(), PamError> {
        // Authenticate via centralised PAM dispatch.
        let ret = match pam_ffi(PamFfi::Authenticate {
            handle: self.handle,
            flags: flags as libc::c_int,
        }) {
            PamFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if ret == ffi::PAM_SUCCESS as i32 {
            Ok(())
        } else {
            Err(PamError::new(ret))
        }
    }

    /// Check account validity after successful authentication.
    ///
    /// Calls `pam_acct_mgmt()` to verify account restrictions such as
    /// time-of-day access controls, expired accounts, or disabled accounts.
    /// Should be called after a successful [`authenticate`](PamHandle::authenticate).
    ///
    /// # Errors
    ///
    /// Returns [`PamError`] with the PAM error code if the account check
    /// fails.
    pub fn acct_mgmt(&self, flags: i32) -> Result<(), PamError> {
        // Account management check via centralised PAM dispatch.
        let ret = match pam_ffi(PamFfi::AcctMgmt {
            handle: self.handle,
            flags: flags as libc::c_int,
        }) {
            PamFfiResult::Code(c) => c,
            _ => unreachable!(),
        };
        if ret == ffi::PAM_SUCCESS as i32 {
            Ok(())
        } else {
            Err(PamError::new(ret))
        }
    }

    /// Get a human-readable error string for a PAM error code.
    ///
    /// Calls `pam_strerror()` which returns a static (or internally managed)
    /// C string describing the error. The string is copied into an owned
    /// [`String`].
    pub fn strerror(&self, errnum: i32) -> String {
        // Error string retrieval via centralised PAM dispatch.
        match pam_ffi(PamFfi::Strerror {
            handle: self.handle,
            errnum: errnum as libc::c_int,
        }) {
            PamFfiResult::Str(s) => s,
            _ => unreachable!(),
        }
    }
}

impl Drop for PamHandle {
    fn drop(&mut self) {
        // Release PAM resources via centralised dispatch.
        if !self.handle.is_null() {
            pam_ffi(PamFfi::End {
                handle: self.handle,
                status: ffi::PAM_SUCCESS as libc::c_int,
            });
            self.handle = ptr::null_mut();
        }
        if !self.conv_data_ptr.is_null() {
            pam_ffi(PamFfi::ReclaimConvData {
                ptr: self.conv_data_ptr,
            });
            self.conv_data_ptr = ptr::null_mut();
        }
    }
}

