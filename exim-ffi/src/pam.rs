/// Conversation data passed to the PAM conversation callback.
///
/// Contains the authentication credential strings that will be provided
/// to PAM when it sends `PAM_PROMPT_ECHO_ON` or `PAM_PROMPT_ECHO_OFF`
/// message types. Credentials are consumed sequentially via `current_index`.
///
/// This struct is heap-allocated and its raw pointer is passed to PAM via
/// `pam_conv.appdata_ptr`. It must remain at a stable address for the
/// entire lifetime of the owning [`PamHandle`].
pub struct PamConversationData {
    /// Authentication credential strings provided to PAM prompts.
    pub responses: Vec<String>,
    /// Index of the next credential to provide when PAM requests one.
    pub current_index: usize,
}

// ---------------------------------------------------------------------------
// Conversation callback (extern "C")
// ---------------------------------------------------------------------------

/// Extern "C" conversation callback function required by libpam.
///
/// PAM calls this function during `pam_authenticate` to request credentials.
/// The function receives prompt messages and returns allocated responses.
///
/// # Safety invariants (guaranteed by the calling context)
///
/// * `msg` is a valid pointer to an array of `num_msg` message pointers.
/// * `resp` is a valid out-pointer where we must place `malloc`'d responses.
/// * `appdata_ptr` is our `PamConversationData` cast to `*mut c_void`,
///   created in [`PamHandle::start_with_credentials`] from a `Box`.
///
/// Responses MUST be allocated with `libc::malloc` because PAM takes
/// ownership and will call `free()` on them. Using Rust's allocator would
/// cause undefined behaviour when PAM calls `free()`.
extern "C" fn pam_conversation(
    num_msg: libc::c_int,
    msg: *mut *const ffi::pam_message,
    resp: *mut *mut ffi::pam_response,
    appdata_ptr: *mut libc::c_void,
) -> libc::c_int {
    // SAFETY: This entire function is an extern "C" callback
    // invoked by libpam during pam_authenticate(). We must:
    //   1. Dereference `msg` (a C-provided pointer array) to read prompts
    //   2. Dereference `appdata_ptr` (our pinned PamConversationData)
    //   3. Allocate responses with libc::malloc (PAM calls free())
    //   4. Copy credential strings with libc::strdup (same reason)
    // All pointer dereferences are guarded by null checks.
    unsafe {
        // Guard against null pointers from the PAM library.
        if appdata_ptr.is_null() || msg.is_null() || resp.is_null() || num_msg <= 0 {
            return ffi::PAM_CONV_ERR as libc::c_int;
        }

        let conv_data = &mut *(appdata_ptr as *mut PamConversationData);
        let num = num_msg as usize;

        // Allocate response array with libc::malloc. PAM takes ownership.
        let reply_size = std::mem::size_of::<ffi::pam_response>()
            .checked_mul(num)
            .unwrap_or(0);
        if reply_size == 0 {
            return ffi::PAM_CONV_ERR as libc::c_int;
        }
        let reply = libc::malloc(reply_size) as *mut ffi::pam_response;
        if reply.is_null() {
            return ffi::PAM_CONV_ERR as libc::c_int;
        }

        // Zero-initialise the response array.
        ptr::write_bytes(reply, 0, num);

        for i in 0..num {
            let message = *msg.add(i);
            if message.is_null() {
                free_partial_responses(reply, i);
                return ffi::PAM_CONV_ERR as libc::c_int;
            }

            let msg_style = (*message).msg_style as u32;
            let response = reply.add(i);

            match msg_style {
                ffi::PAM_PROMPT_ECHO_ON | ffi::PAM_PROMPT_ECHO_OFF => {
                    // PAM requests a credential. Provide the next one.
                    if conv_data.current_index < conv_data.responses.len() {
                        let cred = &conv_data.responses[conv_data.current_index];
                        conv_data.current_index += 1;

                        match CString::new(cred.as_str()) {
                            Ok(c_str) => {
                                (*response).resp =
                                    libc::strdup(c_str.as_ptr()) as *mut libc::c_char;
                            }
                            Err(_) => {
                                // Interior null byte — provide empty string.
                                (*response).resp = libc::strdup(c"".as_ptr()) as *mut libc::c_char;
                            }
                        }
                    } else {
                        // No more credentials — empty string (C behaviour).
                        (*response).resp = libc::strdup(c"".as_ptr()) as *mut libc::c_char;
                    }
                    (*response).resp_retcode = 0;
                }
                ffi::PAM_TEXT_INFO | ffi::PAM_ERROR_MSG => {
                    // Informational / error — acknowledge silently.
                    (*response).resp = ptr::null_mut();
                    (*response).resp_retcode = 0;
                }
                _ => {
                    // Unknown message style — abort conversation.
                    free_partial_responses(reply, i);
                    return ffi::PAM_CONV_ERR as libc::c_int;
                }
            }
        }

        *resp = reply;
        ffi::PAM_SUCCESS as libc::c_int
    }
}

/// Free responses that have already been filled in when the conversation
/// callback encounters an error partway through the message array.
///
/// # Safety
///
/// `reply` must be a valid pointer to an array of at least `count`
/// `pam_response` structs allocated with `libc::malloc`. Each element's
/// `resp` field is either null or was allocated with `libc::strdup`.
unsafe fn free_partial_responses(reply: *mut ffi::pam_response, count: usize) {
    for j in 0..count {
        let prev = reply.add(j);
        if !(*prev).resp.is_null() {
            libc::free((*prev).resp as *mut libc::c_void);
        }
    }
    libc::free(reply as *mut libc::c_void);
}

