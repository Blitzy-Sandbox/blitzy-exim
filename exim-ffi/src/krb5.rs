/// Result of a single GSSAPI security context establishment step.
///
/// The GSSAPI token exchange is multi-step: the server and client exchange
/// tokens until both sides agree that authentication is complete.
pub enum GssContextStepResult {
    /// More tokens are needed from the client. The contained `Vec<u8>` is the
    /// output token that must be sent back to the client.
    Continue(Vec<u8>),
    /// Authentication is complete. The contained `Vec<u8>` is the final output
    /// token (possibly empty) to send to the client.
    Done(Vec<u8>),
}

// =============================================================================
// GssContext — Safe wrapper around gss_ctx_id_t
// =============================================================================

/// Safe wrapper around a GSSAPI security context (`gss_ctx_id_t`).
///
/// Manages the lifecycle of a GSSAPI security context, ensuring that
/// `gss_delete_sec_context` is called when the context is dropped.
///
/// Usage mirrors the server-side authentication flow in
/// `src/src/auths/heimdal_gssapi.c` lines 227–538.
pub struct GssContext {
    /// The raw GSSAPI context handle. Starts as null (`GSS_C_NO_CONTEXT`)
    /// and is populated by `gss_accept_sec_context`.
    ctx: ffi::gss_ctx_id_t,
}

impl Default for GssContext {
    fn default() -> Self {
        Self::new()
    }
}

impl GssContext {
    /// Create a new empty security context for server-side authentication.
    ///
    /// The context starts in the `GSS_C_NO_CONTEXT` state and is populated
    /// by calling [`accept`](Self::accept) with client tokens.
    pub fn new() -> Self {
        // GSS_C_NO_CONTEXT is defined as ((gss_ctx_id_t) 0) in gssapi.h,
        // which is a null pointer in Rust.
        Self {
            ctx: ptr::null_mut(),
        }
    }

    /// Accept a security context token from the client (server-side).
    ///
    /// Processes one step of the GSSAPI server-side authentication by calling
    /// `gss_accept_sec_context`. This matches the token exchange loop in
    /// `src/src/auths/heimdal_gssapi.c` lines 343–378.
    ///
    /// # Arguments
    ///
    /// * `cred` — Server credentials obtained via [`GssCredential::acquire_accept`].
    /// * `input_token` — Raw token bytes received from the client.
    ///
    /// # Returns
    ///
    /// A tuple of:
    /// - `GssContextStepResult::Continue(token)` if more tokens are needed, or
    ///   `GssContextStepResult::Done(token)` when authentication is complete.
    /// - `Option<GssName>` — The authenticated client identity, present when
    ///   authentication completes (`Done` variant).
    ///
    /// # Errors
    ///
    /// Returns `GssapiError` if `gss_accept_sec_context` fails.
    pub fn accept(
        &mut self,
        cred: &GssCredential,
        input_token: &[u8],
    ) -> Result<(GssContextStepResult, Option<GssName>), GssapiError> {
        let mut min_stat: u32 = 0;
        let mut src_name: ffi::gss_name_t = ptr::null_mut();
        let mut mech_type: ffi::gss_OID = ptr::null_mut();
        let mut output_buf = ffi::gss_buffer_desc_struct {
            length: 0,
            value: ptr::null_mut(),
        };
        let mut ret_flags: u32 = 0;
        let mut time_rec: u32 = 0;
        let mut delegated_cred: ffi::gss_cred_id_t = ptr::null_mut();

        // Create a GSSAPI buffer descriptor pointing to the input token bytes.
        // The buffer is read-only from GSSAPI's perspective; the const cast is
        // required by the C API which takes `gss_buffer_t` (pointer to mutable
        // buffer descriptor) even for input buffers.
        let mut input_buf = ffi::gss_buffer_desc_struct {
            length: input_token.len(),
            value: input_token.as_ptr() as *mut libc::c_void,
        };

        // SAFETY: calling gss_accept_sec_context to process one
        // step of the GSSAPI server-side authentication. All pointer arguments
        // point to valid stack-allocated variables or valid handles:
        // - `self.ctx`: either GSS_C_NO_CONTEXT (first call) or a valid context
        //   from a prior call.
        // - `cred.cred`: valid credential from gss_acquire_cred.
        // - `input_buf`: points to valid input_token bytes that outlive this call.
        // - Output pointers (src_name, mech_type, output_buf, ret_flags,
        //   time_rec, delegated_cred) are all valid stack-allocated variables.
        let maj = unsafe {
            ffi::gss_accept_sec_context(
                &mut min_stat,
                &mut self.ctx,
                cred.cred,
                &mut input_buf,
                ptr::null_mut(), // GSS_C_NO_CHANNEL_BINDINGS
                &mut src_name,
                &mut mech_type,
                &mut output_buf,
                &mut ret_flags,
                &mut time_rec,
                &mut delegated_cred,
            )
        };

        // Release any delegated credentials we don't use.
        if !delegated_cred.is_null() {
            let mut rel_min: u32 = 0;
            // SAFETY: releasing delegated credentials that we do
            // not need. The handle is valid because gss_accept_sec_context
            // returned it.
            unsafe {
                ffi::gss_release_cred(&mut rel_min, &mut delegated_cred);
            }
        }

        if is_gss_error(maj) {
            // If gss_accept_sec_context allocated an output buffer before
            // failing, release it.
            if output_buf.length > 0 && !output_buf.value.is_null() {
                let mut rel_min: u32 = 0;
                // SAFETY: releasing the output buffer on error.
                unsafe {
                    ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
                }
            }
            // Release any source name allocated before the error.
            if !src_name.is_null() {
                let mut rel_min: u32 = 0;
                // SAFETY: releasing the source name on error.
                unsafe {
                    ffi::gss_release_name(&mut rel_min, &mut src_name);
                }
            }
            return Err(GssapiError::from_status(maj, min_stat));
        }

        // Copy the output token bytes to an owned Vec before releasing the
        // GSSAPI-managed buffer.
        let output_token = if output_buf.length > 0 && !output_buf.value.is_null() {
            // SAFETY: the output buffer was populated by
            // gss_accept_sec_context on success. The value pointer is valid
            // for `length` bytes.
            let bytes = unsafe {
                std::slice::from_raw_parts(output_buf.value as *const u8, output_buf.length)
            };
            let token = bytes.to_vec();

            let mut rel_min: u32 = 0;
            // SAFETY: releasing the output buffer after copying
            // its contents. The buffer descriptor is valid.
            unsafe {
                ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
            }

            token
        } else {
            Vec::new()
        };

        // Wrap the source name if it was returned.
        let client_name = if !src_name.is_null() {
            Some(GssName { name: src_name })
        } else {
            None
        };

        // Determine whether authentication is complete or continuing.
        let step_result = if maj == ffi::GSS_S_COMPLETE {
            GssContextStepResult::Done(output_token)
        } else {
            // GSS_S_CONTINUE_NEEDED (or other supplementary info)
            GssContextStepResult::Continue(output_token)
        };

        Ok((step_result, client_name))
    }

    /// Query the authenticated client name from a completed security context.
    ///
    /// Calls `gss_inquire_context` to retrieve the source (client) name from
    /// a fully established security context. This is an alternative to
    /// capturing the source name from the final [`accept`](Self::accept) call.
    pub fn inquire_source_name(&self) -> Result<GssName, GssapiError> {
        let mut min_stat: u32 = 0;
        let mut src_name: ffi::gss_name_t = ptr::null_mut();
        let mut targ_name: ffi::gss_name_t = ptr::null_mut();
        let mut lifetime: u32 = 0;
        let mut mech_type: ffi::gss_OID = ptr::null_mut();
        let mut ctx_flags: u32 = 0;
        let mut locally_initiated: i32 = 0;
        let mut open: i32 = 0;

        // SAFETY: calling gss_inquire_context to retrieve the
        // source (client) name from a fully established security context.
        // `self.ctx` is a valid context handle populated by prior calls to
        // gss_accept_sec_context. All output pointers are valid stack-allocated
        // variables. The target name is released below since we only need the
        // source name.
        let maj = unsafe {
            ffi::gss_inquire_context(
                &mut min_stat,
                self.ctx,
                &mut src_name,
                &mut targ_name,
                &mut lifetime,
                &mut mech_type,
                &mut ctx_flags,
                &mut locally_initiated,
                &mut open,
            )
        };

        // Release the target name — we only need the source name.
        if !targ_name.is_null() {
            let mut rel_min: u32 = 0;
            // SAFETY: releasing the target name returned by
            // gss_inquire_context that we do not need.
            unsafe {
                ffi::gss_release_name(&mut rel_min, &mut targ_name);
            }
        }

        if is_gss_error(maj) {
            // Release source name on error if it was partially allocated.
            if !src_name.is_null() {
                let mut rel_min: u32 = 0;
                // SAFETY: releasing partially allocated source name.
                unsafe {
                    ffi::gss_release_name(&mut rel_min, &mut src_name);
                }
            }
            return Err(GssapiError::from_status(maj, min_stat));
        }

        if src_name.is_null() {
            return Err(GssapiError {
                major: 0,
                minor: 0,
                message: "gss_inquire_context returned null source name".to_string(),
            });
        }

        Ok(GssName { name: src_name })
    }

    /// Wrap a message with integrity and optional confidentiality protection.
    ///
    /// Wraps the input message using `gss_wrap()` on a fully established
    /// security context. Used for the SASL GSSAPI security-layer negotiation
    /// where the server sends wrapped capability bytes to the client.
    ///
    /// Replaces the `gss_wrap()` call at `heimdal_gssapi.c` line 393.
    ///
    /// # Arguments
    ///
    /// * `message` — The plaintext message bytes to wrap.
    /// * `confidential` — If `true`, request confidentiality (encryption).
    ///   If `false`, request integrity protection only.
    ///
    /// # Returns
    ///
    /// The wrapped message as a `Vec<u8>`.
    ///
    /// # Errors
    ///
    /// Returns `GssapiError` if `gss_wrap()` fails.
    pub fn wrap(&self, message: &[u8], confidential: bool) -> Result<Vec<u8>, GssapiError> {
        let mut min_stat: u32 = 0;
        let mut conf_state: i32 = 0;
        let mut output_buf = ffi::gss_buffer_desc_struct {
            length: 0,
            value: ptr::null_mut(),
        };
        let mut input_buf = ffi::gss_buffer_desc_struct {
            length: message.len(),
            value: message.as_ptr() as *mut libc::c_void,
        };

        let conf_req = if confidential { 1 } else { 0 };

        // SAFETY: calling gss_wrap to protect a message with
        // the established GSSAPI security context. `self.ctx` is a valid
        // context handle from a completed gss_accept_sec_context exchange.
        // `input_buf` points to valid `message` bytes that outlive this call.
        // `output_buf` and `conf_state` are valid stack-allocated variables.
        // `qop_req` of 0 means use default QOP.
        let maj = unsafe {
            ffi::gss_wrap(
                &mut min_stat,
                self.ctx,
                conf_req,
                0, // GSS_C_QOP_DEFAULT
                &mut input_buf,
                &mut conf_state,
                &mut output_buf,
            )
        };

        if is_gss_error(maj) {
            if output_buf.length > 0 && !output_buf.value.is_null() {
                let mut rel_min: u32 = 0;
                // SAFETY: releasing the output buffer on error.
                unsafe {
                    ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
                }
            }
            return Err(GssapiError::from_status(maj, min_stat));
        }

        // Copy the wrapped bytes and release the GSSAPI-managed buffer.
        let result = if output_buf.length > 0 && !output_buf.value.is_null() {
            // SAFETY: the output buffer was populated by gss_wrap
            // on success. The value pointer is valid for `length` bytes.
            let bytes = unsafe {
                std::slice::from_raw_parts(output_buf.value as *const u8, output_buf.length)
            };
            let token = bytes.to_vec();

            let mut rel_min: u32 = 0;
            // SAFETY: releasing the output buffer after copying.
            unsafe {
                ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
            }
            token
        } else {
            Vec::new()
        };

        Ok(result)
    }

    /// Unwrap a message protected by gss_wrap.
    ///
    /// Unwraps the input token using `gss_unwrap()` on a fully established
    /// security context. Used for the SASL GSSAPI security-layer negotiation
    /// where the server unwraps the client's security-layer choice response.
    ///
    /// Replaces the `gss_unwrap()` call at `heimdal_gssapi.c` line 423.
    ///
    /// # Arguments
    ///
    /// * `token` — The wrapped message bytes to unwrap.
    ///
    /// # Returns
    ///
    /// The unwrapped plaintext message as a `Vec<u8>`.
    ///
    /// # Errors
    ///
    /// Returns `GssapiError` if `gss_unwrap()` fails.
    pub fn unwrap(&self, token: &[u8]) -> Result<Vec<u8>, GssapiError> {
        let mut min_stat: u32 = 0;
        let mut conf_state: i32 = 0;
        let mut qop_state: u32 = 0;
        let mut output_buf = ffi::gss_buffer_desc_struct {
            length: 0,
            value: ptr::null_mut(),
        };
        let mut input_buf = ffi::gss_buffer_desc_struct {
            length: token.len(),
            value: token.as_ptr() as *mut libc::c_void,
        };

        // SAFETY: calling gss_unwrap to decode a wrapped message
        // with the established GSSAPI security context. `self.ctx` is a valid
        // context handle from a completed gss_accept_sec_context exchange.
        // `input_buf` points to valid `token` bytes that outlive this call.
        // `output_buf`, `conf_state`, and `qop_state` are valid stack vars.
        let maj = unsafe {
            ffi::gss_unwrap(
                &mut min_stat,
                self.ctx,
                &mut input_buf,
                &mut output_buf,
                &mut conf_state,
                &mut qop_state,
            )
        };

        if is_gss_error(maj) {
            if output_buf.length > 0 && !output_buf.value.is_null() {
                let mut rel_min: u32 = 0;
                // SAFETY: releasing the output buffer on error.
                unsafe {
                    ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
                }
            }
            return Err(GssapiError::from_status(maj, min_stat));
        }

        // Copy the unwrapped bytes and release the GSSAPI-managed buffer.
        let result = if output_buf.length > 0 && !output_buf.value.is_null() {
            // SAFETY: the output buffer was populated by gss_unwrap
            // on success. The value pointer is valid for `length` bytes.
            let bytes = unsafe {
                std::slice::from_raw_parts(output_buf.value as *const u8, output_buf.length)
            };
            let token = bytes.to_vec();

            let mut rel_min: u32 = 0;
            // SAFETY: releasing the output buffer after copying.
            unsafe {
                ffi::gss_release_buffer(&mut rel_min, &mut output_buf);
            }
            token
        } else {
            Vec::new()
        };

        Ok(result)
    }
}

impl Drop for GssContext {
    fn drop(&mut self) {
        if !self.ctx.is_null() {
            let mut min_stat: u32 = 0;
            // SAFETY: calling gss_delete_sec_context to release
            // the GSSAPI security context resources. `self.ctx` is a valid
            // context handle that was populated by gss_accept_sec_context and
            // has not been deleted yet. Passing null for the output token
            // (GSS_C_NO_BUFFER) discards any final token.
            unsafe {
                ffi::gss_delete_sec_context(
                    &mut min_stat,
                    &mut self.ctx,
                    ptr::null_mut(), // GSS_C_NO_BUFFER
                );
            }
        }
    }
}

