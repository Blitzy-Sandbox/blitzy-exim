/// Check whether a GSSAPI major status code indicates an error.
///
/// Equivalent to the C macro `GSS_ERROR(x)` from `<gssapi/gssapi.h>`:
/// ```c
/// #define GSS_ERROR(x) \
///   ((x) & ((0177ul << GSS_C_ROUTINE_ERROR_OFFSET) | \
///            (0177ul << GSS_C_CALLING_ERROR_OFFSET)))
/// ```
///
/// Errors occupy the upper 16 bits of the major status; supplementary info
/// (e.g. `GSS_S_CONTINUE_NEEDED`) occupies the lower 16 bits and is not
/// considered an error.
#[inline]
fn is_gss_error(major: u32) -> bool {
    // Routine errors are in bits 16–23, calling errors in bits 24–31.
    // The mask combines both 7-bit error fields (0o177 = 0x7F per field).
    const ROUTINE_MASK: u32 = 0x7F << 16;
    const CALLING_MASK: u32 = 0x7F << 24;
    major & (ROUTINE_MASK | CALLING_MASK) != 0
}

// =============================================================================
// GssapiError — Error type for GSSAPI operations
// =============================================================================

/// GSSAPI error with major and minor status codes and a human-readable message.
///
/// Major status codes encode calling errors (bits 24–31), routine errors
/// (bits 16–23), and supplementary info (bits 0–15). Minor status codes
/// are mechanism-specific and provide additional detail.
#[derive(Debug, Clone)]
pub struct GssapiError {
    /// GSSAPI major status code (calling errors bits 24–31, routine errors
    /// bits 16–23, supplementary info bits 0–15).
    pub major: u32,
    /// GSSAPI minor (mechanism-specific) status code providing additional
    /// detail about the failure cause.
    pub minor: u32,
    /// Human-readable error description obtained from `gss_display_status`.
    pub message: String,
}

impl GssapiError {
    /// Create an error from GSSAPI major/minor status codes.
    ///
    /// Calls `gss_display_status` in a loop to convert both the major
    /// (GSS-level) and minor (mechanism-level) status codes into
    /// human-readable messages, then concatenates them.
    pub fn from_status(major: u32, minor: u32) -> Self {
        let mut parts: Vec<String> = Vec::new();

        // Collect major (GSS-level) status messages.
        collect_status_messages(major, ffi::GSS_C_GSS_CODE as i32, &mut parts);

        // Collect minor (mechanism-specific) status messages.
        if minor != 0 {
            collect_status_messages(minor, ffi::GSS_C_MECH_CODE as i32, &mut parts);
        }

        let message = if parts.is_empty() {
            format!("GSSAPI error: major={major:#010x}, minor={minor:#010x}")
        } else {
            parts.join("; ")
        };

        GssapiError {
            major,
            minor,
            message,
        }
    }
}

/// Collect human-readable status messages for a single GSSAPI status value.
///
/// The `gss_display_status` function may produce multiple message fragments
/// for a single status value (indicated by a non-zero `message_context`
/// output). This function loops until all fragments are collected.
fn collect_status_messages(status_value: u32, status_type: i32, out: &mut Vec<String>) {
    let mut msg_ctx: u32 = 0;

    loop {
        let mut min_stat: u32 = 0;
        let mut buf = ffi::gss_buffer_desc_struct {
            length: 0,
            value: ptr::null_mut(),
        };

        // SAFETY: calling gss_display_status to convert a GSSAPI
        // status code into a human-readable string. All pointer arguments point
        // to valid stack-allocated variables. The output buffer is managed by
        // GSSAPI and released via gss_release_buffer below.
        let maj = unsafe {
            ffi::gss_display_status(
                &mut min_stat,
                status_value,
                status_type,
                ptr::null_mut(), // GSS_C_NO_OID — use default mechanism
                &mut msg_ctx,
                &mut buf,
            )
        };

        if !is_gss_error(maj) && buf.length > 0 && !buf.value.is_null() {
            // SAFETY: gss_display_status succeeded and returned
            // a non-empty buffer. The value pointer is valid for `length` bytes
            // as guaranteed by the GSSAPI specification. We create a byte slice
            // from the raw pointer and length, then convert to a Rust String.
            let bytes = unsafe { std::slice::from_raw_parts(buf.value as *const u8, buf.length) };
            let msg = String::from_utf8_lossy(bytes).into_owned();
            out.push(msg);
        }

        if buf.length > 0 && !buf.value.is_null() {
            let mut release_min: u32 = 0;
            // SAFETY: releasing the buffer allocated by
            // gss_display_status. The buffer descriptor is valid because
            // gss_display_status succeeded and populated it.
            unsafe {
                ffi::gss_release_buffer(&mut release_min, &mut buf);
            }
        }

        // msg_ctx == 0 means all message fragments have been returned.
        if msg_ctx == 0 {
            break;
        }
    }
}

impl fmt::Display for GssapiError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.message)
    }
}

impl std::error::Error for GssapiError {}

