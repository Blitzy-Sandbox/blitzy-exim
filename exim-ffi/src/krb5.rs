/// Safe wrapper around a GSSAPI credential (`gss_cred_id_t`).
///
/// Manages the lifecycle of server-side GSSAPI credentials, ensuring that
/// `gss_release_cred` is called when the credential is dropped.
pub struct GssCredential {
    /// The raw GSSAPI credential handle.
    cred: ffi::gss_cred_id_t,
}

impl GssCredential {
    /// Acquire server (acceptor) credentials for the given service name.
    ///
    /// Calls `gss_acquire_cred` with `GSS_C_ACCEPT` usage to obtain
    /// server credentials for the specified service principal. This matches
    /// the credential acquisition in `src/src/auths/heimdal_gssapi.c`
    /// lines 278–289.
    ///
    /// # Arguments
    ///
    /// * `name` — The service name to acquire credentials for, typically
    ///   created via [`GssName::import_service`].
    ///
    /// # Errors
    ///
    /// Returns `GssapiError` if `gss_acquire_cred` fails (e.g., keytab not
    /// found, principal not in keytab).
    pub fn acquire_accept(name: &GssName) -> Result<Self, GssapiError> {
        let mut min_stat: u32 = 0;
        let mut cred: ffi::gss_cred_id_t = ptr::null_mut();

        // Dispatch gss_acquire_cred through consolidated FFI.
        let maj = match krb5_ffi(Krb5Ffi::GssAcquireCred {
            min: &mut min_stat,
            name: name.name,
            treq: 0,                                         // default lifetime
            mechs: ptr::null_mut(),                          // GSS_C_NULL_OID_SET
            usage: ffi::GSS_C_ACCEPT as std::os::raw::c_int, // GSS_C_ACCEPT
            cred: &mut cred,
            amechs: ptr::null_mut(), // not needed
            trec: ptr::null_mut(),   // not needed
        }) {
            Krb5FfiResult::Status(s) => s,
            _ => unreachable!(),
        };

        if is_gss_error(maj) {
            return Err(GssapiError::from_status(maj, min_stat));
        }

        Ok(GssCredential { cred })
    }
}

impl Drop for GssCredential {
    fn drop(&mut self) {
        if !self.cred.is_null() {
            let mut min_stat: u32 = 0;
            // Dispatch gss_release_cred through consolidated FFI.
            krb5_ffi(Krb5Ffi::GssReleaseCred {
                min: &mut min_stat,
                cred: &mut self.cred,
            });
        }
    }
}

