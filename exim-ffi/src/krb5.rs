// register_acceptor_identity — Keytab registration
// =============================================================================

/// Register a keytab file for GSSAPI acceptor (server) identity.
///
/// Sets the keytab that will be used for server-side GSSAPI authentication.
/// This wraps the Heimdal/MIT function `gsskrb5_register_acceptor_identity`
/// (which is a macro aliasing `krb5_gss_register_acceptor_identity`).
///
/// Matches the keytab registration in `src/src/auths/heimdal_gssapi.c`
/// lines 268–276.
///
/// # Arguments
///
/// * `keytab_path` — Filesystem path to the Kerberos keytab file.
///
/// # Errors
///
/// Returns `GssapiError` if the keytab registration fails.
pub fn register_acceptor_identity(keytab_path: &str) -> Result<(), GssapiError> {
    let c_path = CString::new(keytab_path).map_err(|_| GssapiError {
        major: 0xFFFF_0000,
        minor: 0,
        message: "Keytab path contains interior NUL byte".to_string(),
    })?;

    // Dispatch krb5_gss_register_acceptor_identity through consolidated FFI.
    let maj = match krb5_ffi(Krb5Ffi::Krb5RegisterAcceptor {
        path: c_path.as_ptr(),
    }) {
        Krb5FfiResult::Status(s) => s,
        _ => unreachable!(),
    };

    if is_gss_error(maj) {
        // This function has no minor status output, so use 0.
        return Err(GssapiError::from_status(maj, 0));
    }

    Ok(())
}

