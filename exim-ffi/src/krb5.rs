// register_acceptor_identity — Keytab registration
// =============================================================================

/// Register a keytab file for GSSAPI acceptor (server) identity.
///
/// Sets the keytab that will be used for server-side GSSAPI authentication.
/// This wraps the Heimdal/MIT function `gsskrb5_register_acceptor_identity`
/// (which is a macro aliasing `krb5_gss_register_acceptor_identity`).
///
/// Matches the keytab registration in `src/src/auths/heimdal_gssapi.c`
/// lines 268–276.
///
/// # Arguments
///
/// * `keytab_path` — Filesystem path to the Kerberos keytab file.
///
/// # Errors
///
/// Returns `GssapiError` if the keytab registration fails.
pub fn register_acceptor_identity(keytab_path: &str) -> Result<(), GssapiError> {
    let c_path = CString::new(keytab_path).map_err(|_| GssapiError {
        major: 0xFFFF_0000,
        minor: 0,
        message: "Keytab path contains interior NUL byte".to_string(),
    })?;

    // SAFETY: calling krb5_gss_register_acceptor_identity (the
    // real function behind the gsskrb5_register_acceptor_identity macro) to
    // set the Kerberos keytab file path for server-side authentication. The
    // argument is a valid null-terminated C string that outlives this call.
    let maj = unsafe { ffi::krb5_gss_register_acceptor_identity(c_path.as_ptr()) };

    if is_gss_error(maj) {
        // This function has no minor status output, so use 0.
        return Err(GssapiError::from_status(maj, 0));
    }

    Ok(())
}

// =============================================================================
// Krb5Context — Minimal Kerberos 5 context for diagnostics
// =============================================================================

/// Minimal Kerberos 5 context for diagnostics and error reporting.
///
/// Wraps `krb5_context` to provide a safe initialization/teardown lifecycle.
/// Used by the `heimdal_gssapi` authenticator's init function for keytab
/// enumeration and debug output (see `src/src/auths/heimdal_gssapi.c`
/// lines 119–201).
pub struct Krb5Context {
    /// The raw Kerberos 5 context handle.
    ctx: ffi::krb5_context,
}

impl Krb5Context {
    /// Initialize a new Kerberos 5 library context.
    ///
    /// Calls `krb5_init_context` to create a Kerberos library context
    /// suitable for error message formatting and keytab diagnostics.
    ///
    /// # Errors
    ///
    /// Returns `GssapiError` if `krb5_init_context` fails (e.g., Kerberos
    /// configuration is missing or malformed).
    pub fn new() -> Result<Self, GssapiError> {
        let mut ctx: ffi::krb5_context = ptr::null_mut();

        // SAFETY: calling krb5_init_context to initialize a
        // Kerberos library context for error message formatting and
        // diagnostics. The output pointer is a valid stack-allocated variable.
        let rc = unsafe { ffi::krb5_init_context(&mut ctx) };

        if rc != 0 {
            return Err(GssapiError {
                major: 0,
                minor: rc as u32,
                message: format!("krb5_init_context failed with error code {rc}"),
            });
        }

        if ctx.is_null() {
            return Err(GssapiError {
                major: 0,
                minor: 0,
                message: "krb5_init_context returned null context".to_string(),
            });
        }

        Ok(Krb5Context { ctx })
    }
}

impl Drop for Krb5Context {
    fn drop(&mut self) {
        if !self.ctx.is_null() {
            // SAFETY: calling krb5_free_context to release the
            // Kerberos library context. `self.ctx` is a valid krb5_context
            // obtained from krb5_init_context and not yet freed.
            unsafe {
                ffi::krb5_free_context(self.ctx);
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_gss_error_detection() {
        // GSS_S_COMPLETE (0) is not an error.
        assert!(!is_gss_error(ffi::GSS_S_COMPLETE));

        // GSS_S_CONTINUE_NEEDED (1) is supplementary info, not an error.
        assert!(!is_gss_error(ffi::GSS_S_CONTINUE_NEEDED));

        // A routine error in bits 16–23 should be detected.
        assert!(is_gss_error(1 << 16));

        // A calling error in bits 24–31 should be detected.
        assert!(is_gss_error(1 << 24));

        // Combined routine + supplementary is still an error.
        assert!(is_gss_error((1 << 16) | ffi::GSS_S_CONTINUE_NEEDED));
    }

    #[test]
    fn test_gssapi_error_display() {
        let err = GssapiError {
            major: 0x000D_0000,
            minor: 0,
            message: "test error message".to_string(),
        };
        assert_eq!(format!("{err}"), "test error message");
    }

    #[test]
    fn test_gssapi_error_debug() {
        let err = GssapiError::from_status(0, 0);
        // With both codes zero, from_status should produce a fallback message.
        let debug = format!("{err:?}");
        assert!(debug.contains("GssapiError"));
    }

    #[test]
    fn test_context_step_result_variants() {
        let cont = GssContextStepResult::Continue(vec![1, 2, 3]);
        match cont {
            GssContextStepResult::Continue(data) => assert_eq!(data, vec![1, 2, 3]),
            GssContextStepResult::Done(_) => panic!("Expected Continue variant"),
        }

        let done = GssContextStepResult::Done(vec![4, 5]);
        match done {
            GssContextStepResult::Done(data) => assert_eq!(data, vec![4, 5]),
            GssContextStepResult::Continue(_) => panic!("Expected Done variant"),
        }
    }

    #[test]
    fn test_gss_context_new() {
        let ctx = GssContext::new();
        // The context should start as GSS_C_NO_CONTEXT (null pointer).
        assert!(ctx.ctx.is_null());
        // Drop should be safe on a null context.
        drop(ctx);
    }
}
