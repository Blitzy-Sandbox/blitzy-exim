//! Safe wrappers around Heimdal's GSSAPI/Kerberos libraries.
//!
//! Provides managed GSSAPI security contexts for Kerberos authentication,
//! wrapping the GSSAPI C API (`gss_accept_sec_context`, `gss_init_sec_context`,
//! `gss_import_name`, `gss_display_name`, etc.) and Kerberos keytab management
//! via `gsskrb5_register_acceptor_identity`.
//!
//! This module is feature-gated behind `ffi-krb5` and corresponds to the C
//! source file `src/src/auths/heimdal_gssapi.c`. No mature pure-Rust Kerberos
//! client exists (see AAP §0.6.2), so we wrap the system GSSAPI/krb5 C
//! libraries via FFI.
//!
//! # Safety Model
//!
//! All raw GSSAPI handles (`gss_name_t`, `gss_ctx_id_t`, `gss_cred_id_t`,
//! `krb5_context`) are wrapped in RAII structs with `Drop` implementations
//! that call the corresponding release/free function. Every `unsafe` block
//! is documented with an inline justification comment per AAP §0.7.2.

